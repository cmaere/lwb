<div class="accordion-body collapse" id="அறிமுகம்">
<div class="accordion-inner panel-body">
<p style="text-align: justify;">இலங்கையில் பொருளாதார மற்றும் சமூக அபிவிருத்திக்கான அரச நிகழ்ச்சித்திட்டத்தில் அத்தியாவசிய அல்லது அடிப்படை அம்சமாக இருப்பது பாதுகாப்பான குடிநீர் மற்றும் துப்புரவேற்பாட்டு சேவைக்கான அணுகுமுறையை ஏற்படுத்திக் கொடுப்பதாகும். உள்ளடக்கும் நிலைகளும் சேவையின் தரமும் கடந்த தசாப்தம் முழுவதிலும் குறிப்பிடத்தக்களவு உயர்ந்தாலும் நீர் சேவைக்காகவுள்ள தேவை போதியளவு நீரையும் துப்புரவேற்பாட்டையும் (குழாய் மார்க்க மலமகற்றும் வடிகால் முறைமை உட்கட்டமைப்பு வசதிகள் உட்பட) விநியோகிப்பதற்கு நாடு முழுவதிலும் உள்ள பிரசைகள் சமமாக அணுகுவதை உறுதிப்படுத்திக் கொள்வதற்கும் அரசுக்குள்ள ஆற்றல் விஞ்சிச் சென்றுள்ளது. ஆகவே இத் துறையில் புனரமைப்பு பணிகளை மேற்கொள்வதற்கு இலங்கை அரசு நிகழ்ச்சித் திட்டங்களை ஆரம்பித்துள்ளது. இப் புனரமைப்பு செயற்பாட்டின் ஒரு பகுதியாக தற்பொழுது மேற்கொள்ளப்படுகின்ற நடவடிக்கைகளுக்கிடையில் நீர் வழங்கல் மற்றும் துப்புரவேற்பாட்டு முறைமைக்கான ஒழுங்குறுத்தும் ஆணைக்குழுவை ஸ்தாபித்தல் மற்றும் செயற்பாட்டு வினைத்திறனை மேம்படுத்துவதற்கும் நாட்டில் தெரிவுசெய்யப்பட்ட பிரதேசங்களில் தனியார் செயற்பாட்டாளர்களுக்கு ஒப்பந்தங்களைப் பொறுப்பளிப்பதற்கும் தனியார் துறையின் நிதி முதலீடுகளை வழங்குதல் என்பவை சேர்கின்றன. மேலும், கிராமிய நீர் வழங்கல் மற்றும் துப்புரவேற்பாட்டு துணைப்பிரிவுக்காக அரசாங்கத்தின் மத்திய அமைச்சின் கீழ் தனிப் பிரிவு ஒன்று ஸ்தாபிக்கப்பட்டுள்ளது.</p>
<p style="text-align: justify;">நீர் வழங்கல் மற்றும் துப்புரவேற்பாட்டு பிரிவின் வளர்ச்சிக்கு வழிகாட்டும்பொருட்டு தோட்ட வீடமைப்பு மற்றும் உட்கட்டமைப்பு வசதிகள் அமைச்சு அத் துறைக்கான அனைத்து பிரிவுகளையும் உள்ளடக்குகின்ற கொள்கை சட்டகம் ஒன்றைத் தயாரித்துள்ளது. இக் கொள்கை நீர் வழங்கல் மூலம் உள்ளடக்கப்படுகின்ற பிரதேசங்கள், சேவைத் தரம் மற்றும் செலவை மீளப் பெற்றுக்கொள்ளுதல் என்பவை சம்பந்தமாக அரசாங்கத்தின் நோக்கங்களை நிறைவேற்றிக் கொள்வதற்கான நிகழ்ச்சித்திட்டங்கள் மற்றும் முதலீட்டு மூலோபாயங்களைத் திட்டமிடுகின்றபோதும் செயற்படுத்துகின்ற போதும் மகாணசபைகள் மற்றும் உள்ளூராட்சி மன்றங்கள் உள்ளிட்ட அரச முகவர் நிலையங்கள், கடன் கொடுக்கும் நிறுவனங்கள், நீர் வழங்கல் மற்றும் துப்புரவேற்பாட்டு சேவைகளில் ஈடுபட்டிருக்கின்ற சமூக அடிப்படையிலான அமைப்புகள் மற்றும் அரச சார்பற்ற அமைப்புகள் என்பவற்றின் வழிகாட்டலைப் பெறுகின்றது.</p>
<p style="text-align: justify;">தோட்ட வீடமைப்பு மற்றும் உட்கட்டமைப்பு வசதிகள் அமைச்சு, தேசிய நீர் வழங்கல் வடிகாலமைப்பு சபை மற்றும் ஏனைய அமைச்சுகள் மற்றும் அரச முகவர் நிறுவனங்கள் என்பவற்றினால் இப் பிரிவுக்காக இதற்கு முன்னர் தாhரித்துள்ள பல்வேறு கொள்கைகள் இவ் வெளியீட்டைப் பிரதிபலிக்கின்றன. இப் பிரிவுக்காக அரச இலக்குகளை நிறைவேற்றிக் கொள்ளுவதற்கு வசதியளிக்குமுகமாக தோட்ட வீடமைப்பு மற்றும் உட்கட்டமைப்பு வசதிகள் அமைச்சு, இக் கொள்கையை காலத்துக்குக் காலம் இற்றைப்படுத்துகிறது.</p>


