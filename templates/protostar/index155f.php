<h5>முதலீட்டு தெரிவு அளவுகோல்</h5>
<p style="text-align: justify;">பல்வேறு கருத்திட்டங்களைத் தரப்படுத்துவதை முதன்மைப்படுத்தும்பொருட்டு தெரிவுசெய்யும் அளவுகோல்களைத் தயாரிக்க வேண்டும். சனத்தொகை செறிவு, தனிநபர் முதலீட்டு பிரிவுக்கு உரிய நிவாரண உதவி பங்கீடு போன்ற பிரதான தரங்களை அடிப்படையாகக்கொண்டு மாவட்டங்களையும் அதன் பின்னர் நகர நிலையங்களையும் பிரிப்பது தொடர்பாக முன்னுரிமையளிப்பதற்காக இவ் அளவுகோல்களுக்கு முதலில் பரிகாரம் தேட வேண்டும். அத்துடன் நடைமுறையிலுள்ள திட்டங்கள் மற்றும் நடைமுறையிலுள்ள திட்டங்களை மீட்டல் என்பவற்றுக்கு இடையில் பிரித்து ஒதுக்க வேண்டியது அவசியமாகும்.</p>
