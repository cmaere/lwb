<p style="text-align: justify;">நீர் வழங்கல் பிரிவுக்காக 2010ஆம் ஆண்டில் தேவையான நிதியத்தின் அளவு 85 மில்லியன் ரூபாவென தேசிய நீர் வழங்கல், வடிகாலமைப்புச் சபை மதிப்பிட்டுள்ளது. அரசின் படுகடன் அளவின் மீது விதிக்கப்பட்டுள்ள மட்டுப்படுத்தல் காரணமாக அடுத்து வரும் 10 ஆண்டுகளுக்கு நீர் வழங்கலுக்காக மத்திய அரசு ஒதுக்கியுள்ள தொகை 45 பில்லியனாகும். அது அந்த பிரிவுக்குத் தேவையான நிதியத்தின் பாதியாக இருக்கிறது.</p>
<p style="text-align: justify;">அதற்கிடையில் பல்தரப்பு உதவிகளை வழங்குகின்றவர்கள் உட்கட்டமைப்பு வசதிகளின் கருத்திட்ட நிதி முதலீட்டுக்காக அதிகளவில் தனியார் துறைக்கு நீண்டகாலரீதியில் நிதியங்களை வழங்குகின்றது. முதலீட்டு இடைவெளியை நிரப்பும்பொருட்டும் இலங்கையின் தனியார் துறைக்கு பெயர்க் குறிப்பிடப்பட்டுள்ள நீண்டகால பல்தரப்பு நிதியங்களைக் கவரும்பொருட்டும் நீர் வழங்கல் பிரிவுக்காக தனியார் துறையின் முதலீடு அழைக்கப்படுகின்றது.</p>
<h5 style="text-align: justify;">அரச - தனியர் கூட்டுப்பொறுப்பு</h5>
<p style="text-align: justify;">நீர் முறைமைகளை, செயற்படுத்துதல், பராமரித்தல் மற்றும் விரிவாக்குதல் என்பவற்றுக்காக அரசாங்கத்துடன் கூட்டுப் பொறுப்புக்கு வரும்படி தனியார்துறைக்கு அழைப்பு விடுக்கப்படுகிறது. கீழே பட்டியல்படுத்தப்பட்டுள்ளவையும் உட்பட இவ் அரச - தனியார் கூட்டுப்பொறுப்பு பல்வேறு தோற்றங்களை எடுக்க முடியும்.</p>
