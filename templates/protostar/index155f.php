<div class="accordion-inner panel-body">
<p>அரசு தேசிய இலக்குகளைப் பிரகடனப்படுத்தியுள்ளது. '2010 ஆகின்றபோது அனைவருக்கும் பாதுகாப்பான நீர்'</p>
<p>அதன்மூலம் நிறைவேற்றப்படவிருக்கும் இலக்குகள் கீழே தரப்பட்டுள்ளன.</p>
<ul>
<li style="text-align: justify;">2010 ஆகின்றபோது சனத் தொகையில் 95%க்கு தாங்கக்கூடிய விலைக்கு பாதுகாப்பான குடிநீரைப் பெறும்பொருட்டு உள்ளடக்கத்தை அதிகரித்தல்</li>
<li style="text-align: justify;">நகர பிரதேசங்களில் போதியளவு குழாய் நீர் விநியோக மட்டத்தை நிறைவுசெய்துகொள்ளல்</li>
<li style="text-align: justify;">கைத்தொழில் மற்றும் சேவைத் துறையில் 24 மணித்தியாலங்களும் நீர் விநியோகிப்பதற்கான கோரிக்கையை நிறைவுசெய்தல்</li>
</ul>


