<h4>நகர நீர் வழங்கல் கொள்கை</h4>


<div class="nn_sliders accordion panel-group" id="set-nn_sliders-3">
<div class="accordion-group panel">
<div class="accordion-heading panel-heading">
<a class="accordion-toggle" data-toggle="collapse" data-parent="#set-nn_sliders-3" href="http://waterboard.lk/web/index.php?option=com_content&view=category&id=33&Itemid=103&lang=ta&format=feed&type=rss#அறிமுகம்-2"><div class="clo"></div><div class="ope"></div><span class="nn_sliders-toggle-inner">
அறிமுகம்
</span></a>
</div>
<div class="accordion-body collapse" id="அறிமுகம்-2">
<div class="accordion-inner panel-body">
<h5>பொது</h5>
<p style="text-align: justify;">இலங்கையின் இன்றைய நகர சனத்தொகை சுமார் 5.7 மில்லியனாகும். அது முறையே 2000, 2015 மற்றும் 2030ஆம் ஆண்டளவில் 5.8 மில்லியன், 9.5 மில்லியன் மற்றும் 15 மில்லியன்வரை அதிகரிக்கும் என எதிர்பார்க்கப்படுகிறது. அதன் பிரகாரம் பொருத்தமான நகர சனத்தொகை குறித்த வருடங்களில் முறையே 31.5%, 45% மற்றும் 65% வரை அடையலாம். (மூலம்: வீடமைப்பு மற்றும் நகர அபிவிருத்தி தொடர்பான சனாதிபதி செயலணியின் அறிக்கை)</p>
<p style="text-align: justify;">நகர சனத்தொகையை உள்ளடக்குவதற்காகவும் கைத்தொழில், வாணிப, கப்பல், சுற்றுலாத்துறை போன்ற பொருளாதார பிரிவுகளின் கேள்விகளை நிறைவுசெய்யும் பொருட்டு தற்போதைய பாவனையாளர்களுக்கு திருப்திகரமான மட்டத்தில் சேவை வழங்குவதற்காக தற்பொழுது இருக்கின்ற வசதிகளை மேம்படுத்துவதற்காகவும் நகர நீர் வழங்கலுக்காக தேசிய கொள்கையொன்றைத் தயாரித்தல் முதன்மைத் தேவையாக இருக்கிறது.</p>
<p style="text-align: justify;">இங்கு விபரிக்கப்பட்டுள்ள கொள்கையின் தற்போதைய நிலை, எதிர்கால இலக்குகளும் நோக்கங்களும், தெரிவுசெய்யும் அளவுகோல், முதலீட்டு மூலோபாயம் மற்றும் நகர நீர் வழங்கலில் தேசிய தேவையை நிறைவுசெய்யும் பொருட்டு செயற்பாட்டு மற்றும் பராமரிப்பு நடவடிக்கைகள் உட்பட செயற்படுத்தம் செயல்முறை சுருக்கமாக விபரிக்கப்பட்டுள்ளது.</p>
<p style="text-align: justify;">மேலும், இக் கொள்கை தேசிய நீர் வழங்கல், வடிகாலமைப்பு சபையின் திட்டத்தின் நோக்குடனும் செயற்பாட்டுடனும் இணங்கியொழுகுகிறது.</p>
<h5>நகர நீர்வழங்கலின் நிலை</h5>
<h6>நீர் வழங்கல் உள்ளடக்கத்தின் இன்றைய நிலை</h6>
<p style="text-align: justify;">1998ஆம் ஆண்டில் இலங்கையின் மொத்த சனத்தொகை 18.60 மில்லியனாகும். அவற்றிடையே 5 - 6 மில்லியன் நகர பிரதேசங்களில் வாழ்கின்ற அதேவேளையில் சதவீதத்தில் அது 29% மாகும். இன்று இச் சனத்தொகையில் 90%க்கு சுத்தமான குடிநீரைப் பெறக்கூடியதாக இருக்கிறது. அத்துடன் அதில் 67%க்கு குழாய்மூலம் நீர் விநியோகிக்கப்படுகிறது. தே.நீ.வ.வ.சபை நடாத்துகின்ற குழாய் நீர் விநியோக திட்டத்திற்கிடையில் 24 மணிநேர நீர் விநியோக கொள்ளளவு 36%க்கு மாத்திரம் இருக்கிறது. ஏனைய பெரும்பாலான திட்டங்களுக்கு சாதாரணமாக 12 மணித்தியாலங்கள் மாத்திரமே நீர் விநியோகிக்க முடியும்.</p>
<h6>எதிர்கால இலக்குகளும் நோக்கங்களும்</h6>
<p style="text-align: justify;">2010ஆம் ஆண்டளவில் மொத்த நகர சனத்தொகைக்கு பாதுகாப்பான குடிநீர் வழங்க எதிர்பார்க்கப்படுகிறது. 2010ஆம் ஆண்டில் 22.13 மில்லியனாக இருக்கின்ற சனத்தொகையில் நகர சனத்தொகை 8.85 மில்லியனாக இருக்குமென எதிர்பார்க்கப்டுகிறது. அணுகுதல், விநியோக காலவரையறை மற்றும் விநியோகிக்கின்ற நீரின் தரம் சம்பந்தப்பட்ட சேவைமட்டத்தை மேம்படுத்துவது ஏனைய பிரதான நோக்கமாகும்.</p>


