<p style="text-align: justify;">வருடாந்தம் தயாரிக்கப்படுகின்ற போதியளவு மற்றும் அவ்வப்போது மேற்கொள்ளப்படுகின்ற நீர் வழங்கல் மற்றும் துப்புரவேற்பாட்டு பிரிவின் மீளாய்வின் அடிப்படையிலான அபிவிருத்தி திட்டத்தின் பிரகாரம் முன்னுரிமையை ஸ்தாபிக்க வேண்டும். அதை மக்கள் அறிந்துகொள்ளச் செய்ய வேண்டும். மனை பிரிவுக்கான செலவை திரட்டிய அடிப்படையில் கேள்வியின் பிரகாரம் செயற்படுத்துகின்ற அணுகுமுறையுடன் சுகாதார மற்றும் சமூக பொருளாதார தேவைகளுக்காக அபிவிருத்திசெய்யும் பொருட்டு நிதி வழங்கல் முன்னுரிமை ஸ்தாபிக்கப்பட்டுள்ளது. மனை அல்லாத கேள்விக்கு அளவுகோலாக இருப்பது மூலதனத்துக்கு, செயற்பாட்டுக்கு மற்றும் பராமரிப்புக்கு 100% செலவைத் திரட்டுவதாகும்.</p>
<h5>முதலீட்டு சம்பவ நிலை</h5>
<p style="text-align: justify;">உள்ளடக்கம் மற்றும் சேவை விலைக் கழிவை கவனத்திற்கொண்டு நகர பிரிவின் தேவைகளை முன்னுரிமைப் படுத்தியதன் பின்னர், திட்டமிடப்பட்டு வரையறைக்கு முதலீட்டுத் தேவையைத் தீர்மானிக்க வேண்டும். நிதியிடலை பின்வரும் துறைகளுக்கு பிரித்துக் காட்ட முடியும்.</p>
