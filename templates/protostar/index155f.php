<p style="text-align: justify;">அனைத்து தொழில் முயற்களையும் உள்ளடக்கி நடைமுறைப்படுத்துகிற தகவல் தொழில்நுட்ப கருத்திட்டத்திற்குரிய பணிகள் 2009ஆம் ஆண்டில் தொடர்ச்சியாக நடாத்தப்பட்டது. அனைத்து தொழில் முயற்சிகளின் தகவல் தொழில்நட்ப தீர்வுகளுக்கான ஒப்பந்தம் 4.64 மில்லியன் அமெரிக்க டொலர் பணத்திற்கு இந்தியாவின் கொ - ஒப்சன்ஸ் டெக்னொலொஜிஸ் நிறுவனத்திற்கு வழங்கப்பட்டுள்ளது.</p>
<p style="text-align: justify;">அதற்குள் தே.நீ.வ.வ.சபை நிதி வசதிகளை வழங்குவதற்குரிய கணினி மென்பொருள் உபகரணங்களை விநியோகிப்பதும் பொருத்துவதும் அடங்குகின்றன. தே.நீ.வ.வ.ச. இந் நடவடிக்கையை மேற்கொள்ள ஏற்றுக்கொண்டுள்ளதோடு கணினி வன்பொருள் உபகரணங்களை விநியோகிப்பதும் பொருத்துவதும் 2007ஆம் ஆண்டில் நிறைவுசெய்யப்பட்டது.</p>
<p style="text-align: justify;">இதன் ஒப்பந்த தொகை பின்னர் 3.98 மில்லியன் அமெரிக்க டொலர் வரை குறைக்கப்பட்டது.</p>
<p style="text-align: justify;">2009ஆம் ஆண்டில் தீர்வு பூர்த்திசெய்யப்பட்டது. தீர்வு இனிவரும் நாட்களில் நடைமுறைப்படுத்தப்படவிருக்கிறது.</p>
<p style="text-align: justify;">31/12/2009 வரை அதன் திரட்டிய செலவு 3.98 மில்லியன் அமெரிக்க டொலராக இருந்தது. அதற்கு மேலதிகமாக இணையத்தளத்தினூடாக பணம் செலுத்தும் செயல்முறை, பாவனையாளர் முறைப்பாட்டு செயல்முறை மற்றும் சொத்துக்களை முகாமைப்படுத்தும் செயல்முறை என்பவை தொடர்பான தீர்வு கருத்திட்டம் 2009ஆம் ஆண்டில் மேற்கொள்ளப்பட்டது. நிலையான சொத்துக்களின் உண்மைநிலை மதிப்பீட்டு திணைக்களத்தின்மூலம் மேற்கொள்ளப்பட்டது.</p>
<p style="text-align: justify;">நிலையான சொத்துக்கள் அட்டவணை த.தொ.பிரிவினால் வழங்கப்பட்டுள்ளது. பல்வேறு பிரிவுகளினால் த.தொ. பிரிவுக்கு அறிவிக்கப்பட்ட கணினி வன்பொருள் பராமரிப்பு நடவடிக்கை அப் பிரிவினால் மேற்கொள்ளப்பட்டது. பிராந்திய துணை அலுவலகங்கள் உட்பட நாடு முழுவதிலும் முகாமையாளர்களின் அலுவலகங்களை உள்ளடக்கி விரிவான பிராந்திய வலையமைப்பு (Wide Area Network) 2009ஆம் ஆண்டில் தொடர்ச்சியாக நடாத்தப்பட்டது. தனிப்பட்ட வலையமைப்பு ஸ்ரீ லங்கா ரெலிகோம் நிறுவனத்தினால் வழங்கப்பட்டது.</p>
