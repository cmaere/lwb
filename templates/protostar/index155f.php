<p style="text-align: justify;">நகர நீர் வழங்கலுக்கான செலவு திரட்டல் கட்டண வருமானம், செயற்பாட்டு மற்றும் பராமரிப்பு செலவு, பொதுப் பணிகள் செலவு, கடனை மீளச் செலுத்துதல் அல்லது தேய்மானம் என்ற இரண்டில் மிக அதிகமான எதாவது ஒன்றை நிறைவேற்றிக்கொள்ள எதிர்பார்க்கப்படுகிறது. அவ்வாறே கேள்வி முகாமைத்துவத்திற்காக கடடணம் விதித்தல் மூலம் பரிகாரம் தேடி அதன் மூலம் நீர் வழங்கலின் அடிப்படையில் உள்ள அழுத்தங்களைக் குறைத்து கழிவுப் பொருள் உற்பத்திக்கு குறைந்த பங்களிப்பை வழங்க வேண்டும்.</p>
<p style="text-align: justify;">அரச கொள்கை மற்றும் கண்காணிப்பு சட்டகத்தில் தனியார்துறை பங்களிப்பை ஊக்குவிக்கின்ற அதேவேளையில் அதன்மூலம் முகாமைத்துவ வினைத்திறன் உயரும்.</p>
<p style="text-align: justify;">பயனாளிகளின் செயலூக்கமுள்ள நிதி பங்கேற்பின் மூலம் விநியோக முறைமைக்கான முதலீடு</p>
<p style="text-align: justify;">கடன் / நன்கொடை சதவீதம் நகர பிரதேசங்களில் இன்றைய மட்டத்தின் அடிப்படையைக் கொண்டிருக்க வேண்டும்.</p>
<h5>தனியார்துறையின் பங்கேற்பு</h5>
<p style="text-align: justify;">போதியளவு மற்றும் நம்பிக்கை வைக்கக்கூடிய குழாய் நீர் விநியோகத்திற்கான கேள்வியில் துரித வளர்ச்சியை தற்பொழுது ஏற்படுத்தி கஷ்டங்களை வெற்றிகொள்வதற்கு பிராதான மூலோபாயமாக முகாமைத்துவத்தில் தனியார்துறையின் பங்கேற்பும் நீர் பயன்பாட்டின முதலீடும் அடையாளம் காணப்பட்டுள்ளது.</p>
<p style="text-align: justify;">தேசிய ஆதிக்கத்திற்குப் பதிலாக திறந்த போட்டி நிலவ வேண்டும். அத்துடன் உரிய முதலீட்டைப் பெற்றுக்கொள்தற்காக தனியார்துறையின் பங்கேற்புக்கு உக்குவிப்பளிக்க வேண்டும்.</p>


