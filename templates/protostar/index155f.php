<h5>ஒருங்கிணைக்கப்படாத நகர நீர் வழங்கல்</h5>
<p style="text-align: justify;">சிறு மற்றும் முதலீட்டு நுணுக்கம் மற்றும் கிராமிய நீர்வழங்கல் என்பவற்றக்கான சேவைகளை வழங்குகின்ற ஒருங்கிணக்கப்படாத நகர நீர் வழங்கல் திட்டத்தை அபிவிருத்தி செய்தல் மற்றும் அவற்றுக்குத் தேவையான உதவிகளை வழங்குதல் தே.நீ.வ.வ.சபையினால் தொடர்ச்சியாக மேற்கொள்ளப்படும்.</p>
<h5>கிராமிய நீர் வழங்கல்</h5>
<p style="text-align: justify;">தே.நீ.வ.வ.சபையின் தொழில்நுட்ப உதவியுடன் சிறு கிராமிய நீர் வழங்கல் திட்டத்தை முகாமைப்படுத்துவதை ஏற்றுக்கொள்வதற்கு உள்ளூராட்சி மன்றங்கள், மக்களையும் சிறு தொழில் முயற்சியாளர்களையும் ஊக்கப்படுத்தும்.</p>
<h5>பெரிய வேலைகளையும் தொகைகளையும் விற்றல்</h5>
<p style="text-align: justify;">தனியார்துறை அபிவிருத்தியாளர்கள் செயற்பாட்டாளர்களுக்கு பெரிய வேலைகளை அபிவிருத்தி செய்வதற்காகவும் தொகைகளை விற்பனை செய்வதற்காகவும் தனியார் முதலீடுகள் அழைக்கப்படுகின்ற ஒரு துறையாகும். இம் முதலீடுகளை கட்டியெழுப்புதல், உரிமையை வைத்துக்கொள்தல், செயற்படுத்துதல் மற்றும் ஒப்படைத்தல் (BOOT) போன்ற விதத்தில் அல்லது அதன் இறங்குவரிசை தன்மையைப் பெறலாம். BOOT தயாரிப்பின் கீழ் தனியார்துறை வசதிகளை கட்டியெழுப்பி, பணத்தை ஈடுபடுத்தி, உரிமையை வைத்துக்கொண்டுசெயற்பாடுகளை மேற்கொண்டு இறுதியில் உரித்தை அரசாங்கத்திடம் ஒப்படைக்கிறது. இறங்குவரிசை டீழுழுவு தயாரிப்பின் கீழ் வசதி அரசாங்கத்தால் பணம் ஈடுபடுத்தப்பட்டு நிர்மாணிக்கப்படுகின்ற அதேவேளையில் தனியார் கம்பனி வசதியை செயற்படுத்தி குறிப்பிட்ட ஒரு காலவரையறையில் அரசாங்கத்திற்கு வருடாந்த கட்டணமொன்றைச் செலுத்துவதன் மூலம் அதைக் கொள்வனவு செய்கிறது.</p>
<h5>கழிவு நீர் முறைமை</h5>
<p style="text-align: justify;">நகரமயமாக்கலினதும் கைத்தொழில் மயமாக்கலினதும் சூழலியல் தேவைகளை நிறைவு செய்யும் பொருட்டு கழிவுநீர் முறைமையை மேம்படுத்துவது அத்தியாவசியமானதாகும். கழிவுநீர் முறைமை நீர் வழங்கல் திட்டத்தைவிட மூலதனம் நுணுக்கமானதாக இருக்கின்ற அதேவேளையில் மத்தியகால அடிப்படையில் அரசினால் தொடர்ச்சியாக விநியோகிக்கப்படும். மிகச் சிறந்த சேவையொன்றை வழங்கும் நோக்கில் கழிவுநீர் முறைமையை செயற்படுத்துவதற்கும் பராமரிப்பதற்கும் பாவனையாளர்களிடமிருந்து கட்டணங்களை அறவிடுவதற்கு அரசாங்கம் இக் காலவரையறையில் நடவடிக்கை எடுக்கும். அதற்கிடையில் கழிவுநீர் முறைமையை செயற்படுத்துவதற்கு, பராமரிப்பதை முகாமைப்படுத்தும் பொருட்டு தனியார்துறைக்கு அழைப்பு விடுக்கப்படுகிறது.</p>


