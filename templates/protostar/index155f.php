<h5>நகர நீர் வழங்கல் திட்டம்</h5>
<p>மதிப்பீட்டுக்கான இனம் காணப்பட்டுள்ள ஒருங்கிணைந்த நீர் வழங்கல் முறைமை கீழுள்ளவாறு அமைகிறது.</p>
