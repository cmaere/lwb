<h4 style="text-align: justify;">அடிப்படை பணிகள்</h4>
<ul>
<li style="text-align: justify;">உள்ளூர் நிதியங்களையும் நன்கொடைகளையும் வழங்குகின்றவர்களின் உதவியுடன் நீர் வழங்கல் மற்றும் துப்புரவேற்பாட்டு கருத்திட்டம் தொடர்பாக பரிசீலித்தல், திட்டமிடல், நிர்மாணம் மற்றும் அமைத்தலை கண்காணித்தல், சாத்தியக்கூற்று ஆய்வு, செலவு மதிப்பீடுகளைத் தயாரித்தல் மற்றும் அத்தகைய கருத்திட்டங்களுக்காக சூழலியல் மதிப்பீடுகளை மேற்கொள்தல்.</li>
<li style="text-align: justify;">நுகர்வோருக்கு திருப்திகரமான சேவையை வழங்குவதற்காக நீர் வழங்கல் மற்றும் துப்புரவேற்பாட்டு திட்டங்களை நடைமுறைப்படுத்துதல் மற்றும் பராமரித்தல்</li>
<li style="text-align: justify;">பட்டியல்களைத் தயாரித்தல் மற்றும் தாங்கிக்கொள்ளக்கூடிய கட்டண கட்டமைப்பின் கீழ் பணம் சேகரித்தல்</li>
</ul>
<h4>இரண்டாம் நிலை பணிகள்</h4>
<ul>
<li style="text-align: justify;">மனித வளங்களைத் திட்டமில் மற்றும் அபிவிருத்தி</li>
<li style="text-align: justify;">நவீன தொழில்நுட்ப முறைகளைப் பயன்படுத்தி சேவைகளை மேம்படுத்துவதற்காக ஆய்வுகளையும் அபிவிருத்தியையும் மேற்கொள்தல்</li>
<li style="text-align: justify;">வரவு செலவும் நிதிக் கட்டுப்பாடும்</li>
<li style="text-align: justify;">உற்பத்தித்திறன் மிக்க நீர் பயன்பாடு மற்றும் வருமானம் பெறாத நீர் பயன்பட்டைக் குறைப்பது தொடர்பாக பிரசாரம் செய்தல் மற்றும் நுகர்வோருக்கு அறிவூட்டும் நிகழ்ச்சித்திட்டம்.</li>
<li style="text-align: justify;">நிறுவன திட்டமிடல் மற்றும் மூலோபாய முகாமைத்துவம்</li>
<li style="text-align: justify;">நீர் மற்றும் விரயமாகும் நீரின் நிலையைக் கண்காணிப்பதற்கும் கட்டுப்படுத்தவதற்குமான ஆய்வுகூட சேவை</li>
<li style="text-align: justify;">சமூக அடிப்படையிலான அமைப்புகள் (ச.அ.அ), உள்ளூராட்சி நிறுவனங்கள், அரச முகவர் நிலையங்கள் மற்றும் தனியார் / அரச நிறுவனங்கள் என்பவற்றிற்கான தொழில்நுட்ப உதவி.</li>
<li style="text-align: justify;">நிலக் கீழ் நீர் பரிசோதனை, ஆழமான கிணறு, குழாய் கிணறு / கை குழாய்களைப் பொருத்துதல்</li>
<li style="text-align: justify;">நீர் விநியோகம் மற்றும் சுகாதார பாதுகாப்பு தொடர்பான ஆலோசனை சேவை</li>
<li style="text-align: justify;">கருத்திட்டம் தயாரித்தல் மற்றும் முகாமைத்துவம்</li>
<li style="text-align: justify;">பிரிவின் செயற்பாட்டாளர்கள் மற்றும் தரப்பினருடன் இணைப்பாக்கம்.</li>
<li style="text-align: justify;">தகவல் முகாமைத்துவமும் ஆவணப்படுத்தலும்.</li>
</ul>


