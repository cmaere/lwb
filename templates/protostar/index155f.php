<p style="text-align: justify;">தேசிய நீர் வழங்கல் வடிகாலமைப்பு சபையின் ஆரம்ப கட்டத்தில், அதாவது 1975 முதல் 1990வரையிலான காலப் பகுதியில் நகர பிரதேசங்கள், கிராமங்கள் மற்றும் கைத்தொழில் பேட்டைகளுக்கு குடி நீர் விநியோகிப்பதில் அதிக கவனம் செலுத்தப்பட்டது. எவ்வாறாயினும், நீர் பயன்பாடு அதிகரித்ததாலும் இடத்தைப் பொறுத்தளவில் சுகாதார பாதுகாப்புக்கான போதியளவு நிலம் இல்லாமை காரணமாகவும் கடந்த 10 ஆண்டுகளில் மலமகற்றும் முறைமை தொடர்பிலும் விசேட கவனம் செலுத்தப்பட்டது. தற்பொழுது தே.நீ.வ.வ.சபை கொலொன்னாவ, தெஹிவல- கல்கிஸ்ஸ கொழும்பு பெரும்பகுதி தே.வீ.அ. சபையின் வீட்டுத் திட்டம், ஹந்தானை மற்றும் கதிர்காமம் ஆகிய பிரதேசங்களில் துப்புரவேற்பாட்டு கருத்திட்டங்களை நடாத்துகிறது. கொழும்பிலிருந்து 100 கிலோமீற்றர் தெற்கில் அமைந்துள்ள கரையோர நகரமான ஹிக்கடுவைக்கு அண்மைக் காலமாக குழாய்மூல துப்புரவேற்பாட்டு முறைமைகளை நடாத்துகிறது. கொழும்பிலிருந்து 100 கிலோமீற்றர் தெற்கில் அமைந்துள்ள கரையோர நகரமான ஹிக்கடுவைக்கு அண்மையில் குழாய்மூல துப்புரவேற்பாட்டு திட்டத்தின் செயற்பாட்டு நடவடிக்கைகள் அண்மையில் ஆரம்பிக்கப்பட்டன. கட்டுநாயக்க, சீதாவக்க, பியகம மற்றும் கொக்கல முதலீட்டு ஊக்குவிப்பு வலயங்களில் விரயமாகும் நீரை சேகரித்தல், சுத்திகரித்தல் மற்றும் அகற்றும் முறைமைகள் முதலீட்டு சபையின் சார்பில் தே.நீ.வ.வ.சபையினால் பராமரிக்கப்படுகின்றது. மாவத்தகம, பொல்கஹவெல, மோதரவில மற்றும் மீரிகம முதலீட்டு ஊக்குவிப்பு வலயங்களுக்கான சுத்திகரிக்கும் முறைமை தே.நீ.வ.வ.சபையினால் தற்பொழுது நிர்மாணிக்கப்பட்டுக்கொண்டிருக்கிறது.</p>
<p style="text-align: justify;">கண்டி நகர மத்தியில் சூழல் மாசடைவதைக் குறைப்பதற்காக குழாய்மூலமான துப்புரவேற்பாட்டு கருத்திட்டமொன்று ஜப்பான் அரசாங்கத்தின் உதவியின் கீழ் நடைமுறைப்படுத்தப்படவிருக்கிறது. விசைக் குழாய் மற்றும் சுத்திகரிக்கும் இயந்திர கூடங்களுக்காக காணி உள்ளிட்ட சூழலியல் தாக்க மதிப்பீடு உட்பட சாத்தியக்கூற்று ஆய்வுகள் தற்பொழுது முடிவடைந்து கொண்டிருக்கின்றன. விவசாய திணைக்களத்திற்கு உரிய பேராதெனியில் அமைந்துள்ள காணியொன்றில் சுத்திகரிக்கும் இயந்திரகூடத்தை அமைப்பதற்கு உத்தேசிக்கப்பட்டுள்ளதோடு சுத்திகரித்ததன் பின்னர் கழிவு மகாவலி கங்கைக்கு செலுத்தப்படும்.</p>
<p style="text-align: justify;">ஜாஎல - ஏக்கல மற்றும் மொறட்டுவ இரத்மலான கைத்தொழில் மற்றும் வதிவிட பிரதேசத்திற்காக அண்மையில் சுவீடன் அரசின் உதவியுடன் கருத்திட்டமொன்ற தயாரிக்கப்பட்டது. ஜாஎல ஏக்கல பிரதேசத்தின் வடிகால் கான் முறைமையொன்றும் சுத்திகரிக்கும் இயந்திரகூடம் ஒன்றும் அமைக்கப்படவிருக்கிறது. அத்துடன் நோய்திண்ம சுத்திகரிப்பு இயந்திரகூடமொன்று மொறட்டுவ - இரத்மலான பிரதேசத்துக்காக உத்தேசிக்கப்பட்டுள்ளது. அத்துடன் அது குழாய் மார்க்கமாக கடலுடன் இணைக்கப்படும். உத்தேச கருத்திட்டத்தின் கீழ் நகரம் மற்றும் பல்கலைக் கழகம் உட்பட குறிப்பிடத்தக்க பிரதேசத்தில் விரய நீர் அகற்றப்படும்.</p>
<p style="text-align: justify;">கொழும்பு நகரத்தின் வடக்கிலும் தெற்கிலும் துப்புரவேற்பாட்டு முறைமைகளில் துரிதமாக மேற்கொள்ள வேண்டிய புனரமைப்புக்கான கருத்திட்டம் டென்மார்க் மற்றும் ஒஸ்ட்ரியா அரசாங்கங்களின் உதவியில் அமைக்கப்படவிருக்கிறது. அதன் கீழ் மாதம்பிட்டியில் புதிய விசைக் குழாய் கூடமொன்றை அமைப்பதற்கும் வடக்கு கருத்திட்டத்தின் கீழ் கொள்ளுபிட்டியிலிருந்து மாதம்பிட்டிய வரைக்கும் பிரதான துப்புரவேற்பாட்டு குழாய்களில் 20 கி.மீ புனரமைப்பு உட்பட பிட்டர்சன் ஒழுங்கையில் விசைக் குழாய் கூடத்தைப் புனரமைப்பதற்கு உத்தேசிக்கப்பட்டுள்ளது. அதற்கு மேலதிகமாக தெற்கு கருத்திட்டத்தின் கீழ் வெள்ளவத்தை புதிய விசைக் குழாய் கூடமொன்றையும் துப்புரவேற்பாட்டு வடிகால் முறைமையில் 9 கி.மீற்றரும்; புதுப்பிக்கப்படவிருக்கிறது.</p>
<p style="text-align: justify;">குருநாகல் நகரத்திற்கும் நீர் வழங்கல் மற்றும் துப்புரவேற்பாட்டு கருத்திட்டமொன்று தயாரிக்கப்பட்டுள்ளதோடு அதற்காக டென்மார்க் சர்வதேச முகவர் நிலையத்தின் (டனிடா) கலப்பு கடன் திட்டத்தின் கீழ் நிதியங்களைத் தயாரிக்கும்பொருட்டு அடையாளம் காணப்பட்டுள்ளது. இக் கருத்திட்டம் தற்பொழுது கேள்விப்பத்திர கட்டத்தில் இருக்கிறது.</p>
<p style="text-align: justify;">யாழ்ப்பாண குடாநாட்டில் நீர் வழங்கல் மற்றும் துப்புரவேற்பாட்டு கருத்திட்டத்தின் சாத்தியக்கூற்று ஆய்வுகளை மேற்கொள்ளும் பொருட்டு ஆலோசனை ஒப்படையொன்றை ஆசிய அபிவிருத்தி வங்கி(ஆ.அ.வ) மேற்கொண்டுள்ளது. இவ் ஆய்வு இந்த ஆண்டு பூர்த்திசெய்யப்பட்டுள்ளது. சுனாமி நன்கொடையின் கீழ் கரையோர நகரங்களில் துப்புரவேற்பாட்டு தேவைகளை ஆராய்வதற்காக ஐக்கிய குடியரசு நிதியங்களை வழங்கியுள்ளது.</p>
<p style="text-align: justify;">கொழும்பு பெரும்பாகத்தின் முழு துப்புரவேற்பாட்டு சிக்கல்களை மீளாய்வுசெய்வதற்காக கொழும்பு பெரும்பாக கழிவு நீர் முகாமைத்துவ பிரிவு மீளாய்வுக்காகவும் ஆ.அ.வ. நிதியங்களை வழங்கியுள்ளது. இது தொடர்பிலான மதியுரை நடவடிக்கைகள் 2005ஆம் ஆண்டு இறுதிப்பகுதியில் மேற்கொள்ளப்பட்டது.</p>
<p style="text-align: justify;">இலங்கையின் நிர்வாக தலைநகரான ஸ்ரீ ஜயவர்தனபுர கோட்டே நகரத்திற்கான தே.நீ.வ.வ.ச. சீன அரசாங்கத்தின் உதவியுடன் குழாய் மூலம் மலமகற்றும் முறைமையை அமைத்துக்கொண்டிருக்கிறது. அடிப்படை திட்டங்களையும் கருத்திட்டத்தையும் தயாரிக்கும் பணிகள் பெரும்பாலும் பூர்த்தியடைந்துள்ளது. அத்துடன் ஒப்பந்தம் தொடர்பான பேச்சுவார்த்தை முடிவடைந்ததன் பின்னர் நிர்மாணப் பணிகள் 2009ஆம் ஆண்டு ஆரம்பிக்கப்படவிருக்கிறது. திருகோணமலை, அம்பாறை, காலி, மற்றும் களுத்துறையில் அமைந்துள்ள சுனாமி வீடமைப்பு திட்டத்தில் துப்புரவேற்பாட்டு வசதிகளுக்கு கேள்விப்பத்திர ஆவணங்களைத் தயாரிப்பது உட்பட திட்டங்களும் நிர்மாணப் பணிகளும் பூர்த்திசெய்யப்பட்டுள்ளன. ஸ்ரீ ஜயவர்தனபுர பல்கலைக் கழகம், ஹெவ்லொக் சிட்டி, இரத்மலான சுனாமி வீடமைப்பு திட்டம், தெல்லிப்பளை புற்றுநோய் வைத்தியசாலை மற்றும் ரனால வீடமைப்பு திட்டம் என்பவற்றுக்கும் இத்தகைய திட்டங்கள் தயாரிக்கப்பட்டுக்கொண்டிருக்கின்றன.</p>
<p style="text-align: justify;">தே.நீ.வ.வ.சபையினால் பராமரிக்கப்படுகின்ற அனைத்து துப்புரவேற்பாட்டு திட்டத்திற்காக 2008 ஏப்பிறல் 01ஆம் திகதி முதல் நடைமுறைப்படுத்தப்படுகின்ற வகையில் தே.நீ.வ.வ.ச. மலமகற்றும் சேவைக் கட்டணமொன்றை விதித்துள்ளது. தற்பொழுது தே.நீ.வ.வ.ச. முன்னர் குறிப்பிட்ட துப்புரவேற்பாட்டு திட்டங்களுக்கு மேலதிகமாக சுமார் 10,000 வீடமைப்பு அலகுகளைக்கொண்ட ஆறு பாரிய வீடமைப்பு திட்டங்களில் (மத்தேகொட, சொய்சாபுர, ஜயவடனகம, மத்துமகேவத்த, ரந்தொழுகம, ஹந்தான) மலமகற்றும் வடிகால் முறைமையைப் பராமரிக்கின்றது. மேலும், புதிய துப்புரவேற்பாட்டு கருத்திட்டங்களுக்கு கடன் வசதிகளை வழங்க வேண்டுமானால் மலமகற்றும் சேவை கட்டணங்களை அறிமுகப்படுத்த வேண்டும் என வெளிநாட்டு கடன் வழங்கும் நிறுவனங்கள் தெரிவித்துள்ளன. தற்பொழுதுள்ள முறைமைகளின் செயற்பாட்டு மற்றும் பராமரிப்பு செலவுகளைப் பெற்றுக்கொள்ளும் பொருட்டும் ஏனைய நகர பிரதேசங்களுக்கு மலமகற்றும் குழாய் வசதிகளை படிப்படியாக விரிவாக்கும் பொருட்டு இத்தகைய கட்டணத்தை விதிப்பது கட்டாயமானதாக இருக்கிறது.</p>


