<h4>நீர் வழங்கல் மற்றும் துப்புரவேற்பாட்டு தனியார் துறையின் பங்கேற்பு தொடர்பான தேசிய கொள்கை</h4>


<div class="nn_sliders accordion panel-group" id="set-nn_sliders-2">
<div class="accordion-group panel">
<div class="accordion-heading panel-heading">
<a class="accordion-toggle" data-toggle="collapse" data-parent="#set-nn_sliders-2" href="http://waterboard.lk/web/index.php?option=com_content&view=category&id=33&Itemid=103&lang=ta&format=feed&type=rss#நோக்கம்"><div class="clo"></div><div class="ope"></div><span class="nn_sliders-toggle-inner">
