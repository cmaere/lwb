<p style="text-align: justify;">பாவனையாளர் பிணைப்பை பாதுகாக்கும் பொருட்டு சுயாதீன ஒழுங்குமுறைப்படுத்தல் ஸ்தாபிக்கப்படும். அந்த ஒழுங்குமுறைப்படுத்தல் தகைமையுடைய தனிநபர் சபையைக் கொண்டிருக்க வேண்டும். அத்துடன் அவர்களுக்கு பின்வரும் விடயங்களுக்கு சட்டத்தின் மூலம் அதிகாரம் அளிக்கப்படும்.</p>
<ul>
<li style="text-align: justify;">அரசாங்கத்தின் கொள்கை மற்றும் சேவை செலவு மட்டத்தின் அடிப்படையில் கட்டணமுறையை விதித்தல்</li>
<li style="text-align: justify;">அரசாங்கத்துடன் உடன்படிக்கையின் கீழ் தமது சட்டபூர்வமான பொறுப்பை செயற்பாட்டாளர்கள் நிறைவேற்றுகின்றனர் என்பதை உறுதிப்படுத்திக்கொள்தல்.</li>
<li style="text-align: justify;">பொருத்தமான தண்டணையுடன் தரத்தினதும் சேவையினதும் தரத்தை வலியுறுத்துதல்</li>
<li style="text-align: justify;">உயர் வினைத்திறன் தரங்களை மேம்படுத்துதல்</li>
<li style="text-align: justify;">முறைப்பாடுகள் தொடர்பாக நடவடிக்கை எடுத்தல் மற்றும் பிணக்குகளைத் தீர்த்தல்</li>
</ul>
<p style="text-align: justify;">அரச மற்றும் தனியார் துறை ஆகிய இருபிரிவுகளிலும் நீர் விநியோகிப்பவர்கள் அடிப்படையிலான நீதிமன்ற அதிகாரம் ஒழுங்குமுறைப்படுத்துபவர் வசம் இருக்கும்.</p>
