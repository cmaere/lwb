<p style="text-align: justify;">சனத்தொகை, புள்ளிவிபரத் திணைக்களத்தின் 1994ஆம் ஆண்டின் சன பரம்பல் ஆய்வின் பிரகாரம் சனத் தொகையில் 72% க்கு பாதுகாப்பான தோற்றுவாய் ஊடாக பாதுகாப்பான குடிநீரை அணுகக்கூடியதாக உள்ளது. நகர மயமாக்கல் மற்றும் வாழ்க்கைத் தரத்தின் துரித வளர்ச்சியுடன் தரமான நீர் விநியோகத்திற்கான கோரிக்கை 8 - 10% அளவில் உயரும் என மதிப்பிடப்பட்டுள்ளது. பெரும்பாலானோர் எதிர்பார்க்கின்ற விநியோக மட்டமான நாள் முழுவதும் நீர் விநியோகிக்கக்கூடிய சாத்தியம் தற்பொழுதுள்ள நீர் முறைமைகளில் மிகக் குறைந்த அளவுக்கு மாத்திரமே உண்டு.</p>
<p style="text-align: justify;">கைத்தொழில் மற்றும் சேவை துறையில் நிறுவனங்கள் பாரியளவில் நீரைப் பயன்படுத்துகின்ற அதேவேளையில் அது வருமானத்தையும் சுற்றியுள்ள பிரதேசங்களில் தொழில் வாய்ப்புகளையும் உருவாக்குகின்ற முக்கியமான அபிவிருத்தி துறையாகும். அத்தகைய வாணிப நடவடிக்கைகளுக்காக 24 மணித்தியாலமும் மேற்கொள்ளப்படுகின்ற நீர் வழங்கல் அவசியமாகின்றது.</p>
<p style="text-align: justify;">பாதுகாப்பான குடிநீர் மற்றும் கழிவு நீர் தொடர்பாக நடவடிக்கை எடுப்பதற்கு போதியளவு ஏற்பாடுகள், பொது சுகாதாரத்துடன் சம்பந்தப்பட்ட மிக முக்கியமான விடயமாகும். போதியளவு நீர் வழங்கல் முறைமைகள் மற்றும் கழிவு நீரைச் சேகரித்தல் மற்றும் அகற்றுதல் என்பவற்றுக்கான வசதிகள் இல்லாத நகரமயமாக்கல் நீர் விநியோகத்தை அசுத்தமடைச் செய்ய காரணமாக அமையலாம்.</p>


