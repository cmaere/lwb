<h5>பிரிவுக்கான நோக்கு</h5>
<p style="text-align: justify;">இலங்கை மக்களுக்கு பாதுகாப்பான குடிநீரையும் போதியளவு துப்புரவேற்பாட்டு வசதிகளையும் அணுகுவதற்கான வாய்ப்பை ஏற்படுத்துவதன் மூலம் வாழ்க்கைத் தரத்தை உயர்த்துதல், பொருளாதார சுபீட்சத்தை மேம்படுத்துதல், சூழலைப் பாதுகாத்துல் என்பவற்றுக்கு இலங்கை அரசு தன்னை அர்ப்பணித்துக்கொண்டுள்ளது.</p>
<h5 style="text-align: justify;">நீர் வழங்கலின் இலக்கு</h5>
<ul>
<li style="text-align: justify;">2010ஆம் ஆண்டளவில் இலங்கை சனத் தொகையில் 85% க்கும் 2025ஆம் ஆண்டளவில் 100% கும் போதியளவு, பாதுகாப்பான குடிநீரைப் பெறுவதற்கு நடவடிக்கை எடுக்கப்படும்.</li>
<li style="text-align: justify;">2010ஆம் ஆண்டளவில் நகர சனத் தொகையில் 100% க்கும் கிராமிய சனத் தொகையில் .....%கும் குழாய் நீர் வழங்கப்படும்.</li>
<li style="text-align: justify;">சேவை மட்டத்திலும் நீரின் தரத்திலும் நகர மற்றும் கிராமிய பிரதேசங்கள் தேசிய தரத்தை நிறைவுசெய்திருக்கிறது.</li>
</ul>
<p style="text-align: justify;">மொத்தமாக நீரை வழங்குவதிலிருந்து குழாய் மார்க்க வலையமைப்பு மற்றும் நீரைக் கொண்டுசெல்லும் தாங்கிகளுடனான வாகனங்கள், குழாய் கிணறுகள் மற்றும் ஏனைய சமூக விநியோக முறைமைகள் போன்ற ஏனைய முறைகள் மூலம் பாவனையாளர்களுக்கு இறுதியில் நீரை விநியோகித்தல் வரை குடிநீரை வழங்குதல் இக் கொள்கை மூலம் உள்ளடக்கப்படுகின்றது. துப்புரவேற்பாட்டை ஒழுங்கு செய்கின்றபோது அது குழாய் முறைமை ஊடாக மலம் கொண்டுசெல்வதை சேகரித்தல், சமூக அடிப்படையிலான துப்புரவேற்பாட்டு வசதிகள், இடம் சார்ந்த துப்புரவேற்பாட்டு வசதிகள் மற்றும் சூழலுக்குச் செலுத்துவதற்காக கழிவு நீரைச் சுத்தப்படுத்துதல் என்பவற்றை உள்ளடக்குகிறது.</p>
<p style="text-align: justify;">இக் கொள்கை இவ் ஆவணத்தின் இறுதியில் பட்டியல்படுத்தப்பட்டுள்ளது.</p>
<h5 style="text-align: justify;">துப்புரவேற்பாட்டு இலக்கு</h5>
<ul>
<li style="text-align: justify;">2010ஆம் ஆண்டளவில் இலங்கை சனத் தொகையில் 70% க்கும் 2025ஆம் ஆண்டளவில் 100%கும் போதியளவு, துப்புரவேற்பாட்டைப் பெறுவதற்கு நடவடிக்கை எடுக்கப்படும்.</li>
<li style="text-align: justify;">பிரதான நகரங்களுக்கும் தெரிவுசெய்யப்பட்ட வளர்ச்சி நிலையங்களுக்கும் குழாய்மார்க்க துப்புரவேற்பாட்டு முறைமை வழங்கப்படும். மற்றும்,</li>
<li style="text-align: justify;">மலமகற்றும் முறைமையில் அல்லது ஏனைய துப்புரவேற்பாட்டு முறைமையில் தொடர்புபடாத அனைவருக்கும் ஏற்றுக்கொள்ளப்பட்ட இடம்சார்ந்த துப்புரவேற்பாடு இருக்கிறது.</li>
</ul>


