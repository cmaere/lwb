<p style="text-align: justify;">பெரும்பாலான குடியிருப்பாளர்கள் நம்பிக்கை வைக்கக்கூடிய பாதுகாப்பான நீர் தோற்றுவாய்கள் மிக உயர்வாக மதிக்கப்படுகிறது. நீருக்காக ஒதுக்கக்கூடிய அளவு பொதுவாக வருமானத்தின் 5% உச்சமாக மதிப்பிடப்பட்டுள்ளது. கட்டணமுறையை சீராக்குவதற்காக தயாரிக்கப்பட்ட சூத்திரத்தில் உயர்மட்ட நுகர்வுக்காக படிப்படியாக மிக உயர்ந்த கட்டண சதவீதத்தை அறவிட முடியும். ஏனைய விடயங்களுக்கிடையில் போட்டிரீதியான கட்டணமுறையை வழங்குவதன்மூலம் மத்தியகால வாணிப பிரிவிலிருந்து அறவிடப்படுகின்ற ஆதிக்கத்தை இச் சூத்திரம் அடிப்படையாகக் கொண்டிருக்க வேண்டும்.</p>


