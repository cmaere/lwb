<td style="text-align: justify;">If inspection team cannot identify any leak by visual and manual inspection they will inform you to decide whether to request for inspection using specific instruments for leak detections. If you request for inspection using specific instruments for leak detection, you have to make a payment of Rs. 1,680.00 (including VAT) to the relevant Area Engineer Office.</td>
</tr>
