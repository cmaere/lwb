<table>
<tbody>
<tr valign="top">
<td>1.</td>
<td style="text-align: justify;">
<p>Inform the relevant Area Engineer office by using the complaint form</p>
<p>Inspection team will visit your place and they will carry out the initial visual and manual inspection. This initial inspection will be carried out free of charge.</p>
<h5>Tips to identify an internal leak by yourself</h5>
<ul>
<li>Take the meter reading daily at the same time.</li>
<li>Close all the taps and check whether your water meter is still working.</li>
<li>If it is still working then check the cisterns for leaks.<br /><em><strong>How to check your cisterns?</strong></em>
<ul>
<li>Fill up the cistern and close the inlet valve. Mark the water level. Wait for 2 hours and check the level. If it reduces, then the piston of cistern is leaking. Get it repaired as soon as possible. Till it repaired, close the valve (Stop tap) in the cistern supplies line.</li>
<li>Check the ball valves in the cistern. If it is not working properly then water from the cistern will overflow in to the water closet. Get it repaired as soon as possible. Till it repaired close the valve (stop tap) in the cistern supplies line.</li>
</ul>
</li>
<li>Check the surrounding (inside your premises after the water meter) during the night time (as the pressure in the system is high to night you can observe if there is a leak in your garden.), if you can observe wet patches in the garden or wet areas in walls, it can be a leak in a covered pipeline. Check the covered pipe lines in those places and get them repaired.</li>
<li>Check the ball valve in your overhead tank. If it is not functioning properly, your overhead tank can overflow during the night time.</li>
<li>Check the underground sump for leaks.<br /><em><strong>How to check?</strong></em><br />
<ul>
<li>Fill your overhead tank to full.</li>
<li>Then wait till your ground sump get filled</li>
<li>Close the valve after the water meter.</li>
<li>Mark the water level in the ground sump</li>
<li>Do not use water from the sump or do not pump water to the over head tank after closing the valve.</li>
<li>Observe for 24hours. (As you have enough water in your overhead tank you can wait for 24 hours without pumping water to overhead tank.) Some time you will be able to observe reducing the water level after few hours. Then no need to wait for 24 hours.</li>
<li>If the water level reduces, it means your ground sump is leaking. Ground sump should be covered to minimize the evaporation losses. If it is having leaks take necessary measures to get it repaired as soon as possible. (Empty the sump, check for cracks, and repair the cracks with a suitable water proofing material.)</li>
</ul>
</li>
<li>Check your garden taps whether they are turned off tightly to stop drips and functioning properly.</li>
</ul>
</td>
</tr>
