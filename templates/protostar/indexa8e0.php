<h5><span style="font-size: 0.83em; line-height: 1.3em;"><br /> Urgent improvements to Bandarawela and Diyathalawa Water Supply Schemes (6month crash programme) in Badulla District</span></h5>
<p>With Cabinet approval this urgent improvement implemented and completed 30<sup>th</sup> November 2013 to provide 2000 long outsiding new connections in the waiting list for the last eight years.</p>
<h4>Procument plan</h4>
<p>1. Intake improvement at Aluthwela, Diayathalawa, completed - De-Mal construction</p>
<p>2. Intake improvement at Ellathotta, Kahagolla and Rohamton, completed - CECB</p>
<p>3. Improvement at Banadarawela and Diyathalawa WTP, in progress - Isuru Engineering</p>
<p>4. Replacement of low lift pumps at Aluthwela intake, Diyathalawa, in progress - DIMO</p>
<p>5. Replacement of Booster pumps at Aluthwela, Diyathalawa, in progress - Lanka Tech</p>
<p>6. Enlarging of Aluthwela pumping main, in progress</p>
<h4>Water Supply In Chronic Kidney Diseased (CKD) Areas</h4>
<p>The Following CKD areas have been identified and arranged drinking Water Supply by using browsers for drinking and cooking purposes.</p>
<p>Short term proposal has been prepared for the cost of Rs. 81.4 million for Moneragala and Bandarawela Districts.</p>
<p>1.<strong> Girandurukotte</strong> - Theldeniya vasama Hangurankethyaya in Mahiyanganaya DS Division - 17 PE tanks located at identified places to supply drinking water for 95 families.</p>
<p>2. <strong>Kukurampola, Rahathangama and Kumaragama in Buttala DS Division</strong> - 18 PE tanks located at identified places to supply drinking water for 180 families.</p>
<p>3. <strong>Warunagama in Wellawaya DS Division</strong> - A proposal has been prepared to fix Iron and Floride removal Filters to improve Water quality.</p>
