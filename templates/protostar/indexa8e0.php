			<description><![CDATA[<p>Under the present administrative setup of Southern RSC, both the Deputy General Manager (DGM) and the Assistant General Manager (AGM) are looking after the operations and maintenance (O&amp;M) in addition to the development functions. The Manager (O&amp;M) often coordinates with the AGM for his day to day O&amp;M functions. In the case of development functions however like rehabilitation, the involvement of the DGM is quite significant. Budgetary controls are also being done through the DGM’s office.</p>
<p>At present, the number of water supply systems within the respective regions are as follows:</p>
<ul>
<li><strong>Galle Region -</strong> 16 WS Schemes, about 100,052 service connections</li>
<li><strong>Matara Region -</strong> 19 WS Schemes, about 88,547 service connections</li>
<li><strong>Hambantota Region -</strong> 18 WS Schemes, about 93,769 service connections</li>
</ul>
<p>Depending upon the magnitude of service connections, an officer in-charge is being appointed to manage the water systems. In general if there are over 5000 service connections within a water system, then one senior Engineering assistant is appointed as officer in-charge. Otherwise one officer manages several schemes, the number of which depends upon the complexity of the schemes and the geography. The District/ Area Engineer setup is prevailing to facilitate the technical and administrative support to the OICs for their O&amp;M functions. The development that has taken place in Hambantota district in the recent past towards expansion of water supply coverage has been considerably higher than that of the other two districts.</p>
<p>There are two sewerage schemes in operation, one in Galle district and the other in Hambantota district. These are serving specific industrial or commercial purposes and should not be considered as a municipal sewer system in a city.</p>
