<p style="text-align: justify;">&nbsp;</p>
<p style="text-align: justify;"><strong>The Sabaragamuwa Province</strong> is located on the South-Central region of the island and it is made up of the two administrative districts namely, <strong>Rathnapura</strong> and <strong>Kegalle</strong>. This province is made up of mountain ranges valleys, streams and rivers. Both these Districts are strictly agricultural with tea, rubber, paddy and coconut plantations making up their major income generating enterprises. However much agrarian oriented these Districts are, the District of Rathnapura has maintained a reputation from historical times, the world over, for the wealth of gems.</p>
<p style="text-align: justify;">The Sabaragamuwa Province is the eighth largest province of Sri Lanka boasting a population of <strong>1,919,487</strong> spread out over a land mass <strong>4900 sq km</strong> in extent. Area wise Kegalle District covers <strong>1,663 sq km</strong> while Rathnapura District is <strong>3,237 sq km</strong>. Rathnapura District located at an elevation of 500ft above sea level, and is the administrative capital of the Sabaragamuwa Province about 100km from Colombo. Kegalle District might even be considered the gateway to the central hills of Sri Lanka and the western region of the Kegalle district is less than 500 ft from the sea level with the eastern region lying 1000ft above the sea level.</p>
<p style="text-align: justify;"><strong>The Sabaragamuwa Regional Service Support Center (RSC) of NWSDB</strong> was established in <strong>2008</strong>. After reorganizing the RSC in line with provincial boundaries, it was decided to have a separate RSC for Sabaragamuwa. As is the requirement a Deputy General Manager (DGM) heads the RSC. He is supported by an Assistant General Manager (AGM) and other managers in carrying out the administrative and operational functions. The most important of the RSC functions is the Operation and Maintenance (O&amp;M) of water supply schemes. Other key functions of RSC are: Planning and Design work related to Local Projects; Planning work related to Large Scale Projects; Implementation of Rehabilitation and Augmentation work of the existing schemes; Water Resources Management at the regional or local level; as well as Billing and Revenue collection. Its major task is to provide safe drinking water using new technology and a growing water distribution network.</p>
<p style="text-align: justify;">As of the movement RSC is responsible for the provision of pipe-borne water to <strong>80,528 families</strong> of the region. It also handles two Large Scale Foreign Funded Projects and has eleven Small and Medium Scale Projects with the objectives of facilitating more families with safe drinking water hopefully.</p>


