<p style="text-align: justify;">The O&amp;M manager reports to the DGM through the AGM but all RSC functions are monitored by Additional General Manager from H/O. The RSC provides services to the regional offices within their capacity and any additional help as necessary is obtained from H/O through the respective Additional General Manager.</p>


