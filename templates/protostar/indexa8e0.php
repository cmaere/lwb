			<description><![CDATA[<p style="text-align: justify;">In August 2009 the North RSC functions were completely separated from the other RSCs and a DGM was appointed. An AGM was also appointed to take up developing activities. &nbsp;O&amp;M activities come under 02 Regional Offices namely Vavuniya Region and Jaffna Region which are managed by two seperate Regional Managers. &nbsp;The main RSC functions are to provide esstential support to the Regional Offices in day today O&amp;M activities and to guidance and assistance to improve the Water Supply and Sanitation Coverage.</p>
<p style="text-align: justify;">The O&amp;M manager reports to the DGM. Northern RSC functions are monitored by the Additional General Manager for North &amp; Central. &nbsp;The RSC is providing services to the Regional Offices and public in Northern Province within their capacity. &nbsp;Any additional assistance, if necessary, is obtained from H/O through the Additional General Manager (North &amp; Central).</p>
<p style="text-align: justify;">As this is a newly setup unit and due to the extraordinary situation that has prevailed for nearly three decades, the coverage of NWSDB in the North is limited. There are some medium scale schemes operational in Vavuniya and Mannar district. All other schemes in North RSC are very small scale water schemes.</p>
<p style="text-align: justify;">The NWSDB is now taking steps to establish the North RSC as an independent unit, providing it with support in order to be able to widen and improve the services to the consumers in the North.</p>


