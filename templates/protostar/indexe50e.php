		<summary type="html">&lt;p style=&quot;text-align: justify;&quot;&gt;The Regional Support Center (RSC) Western Province was established in year 1992. With the expansion of water supply facilities in Western Province, the Western Province was divided in to three RSCS’ namely Western Central, Western North and Western South in year 2008.&lt;/p&gt;
&lt;p style=&quot;text-align: justify;&quot;&gt;RSC (W-S) Covers Dehiwala , Moratuwa &amp;amp; Rathmalana DS divisions in Colombo district, and all DS divisions in Kaluthara district &amp;amp; part of Bentota DS division in Galle district.&lt;/p&gt;
&lt;p style=&quot;text-align: justify;&quot;&gt;RSC (WS) is headed by DGM &amp;amp; assistant by AGM. The Operational activities within RSC (W-S) is divided in to three region (Dehiwala, Panadura/Horana, Kalutara) &amp;amp; each region is headed by regional Manager. Total staff within the RSC is about 795 at the end June 2015.&lt;/p&gt;
&lt;p style=&quot;text-align: justify;&quot;&gt;At present, RSC (W-S) provides water supply to about 854,623 persons through 213,257 service connections about the end of June 2015. RSC (W-S) generated Rs.1.947 billion revenue in year 2014, which is about 11% of total revenue of NWSBD.&lt;br /&gt; &lt;br /&gt;Eng. T.W.S Perera functions as the DGM &amp;amp; Eng. P. Ramawickrama functions as the AGM.&lt;/p&gt;
