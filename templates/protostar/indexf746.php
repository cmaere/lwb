		<summary type="html">&lt;h4&gt;History of the National Water Supply &amp;amp; Drainage Board&lt;/h4&gt;
&lt;table&gt;
&lt;tbody&gt;
&lt;tr&gt;
&lt;td style=&quot;text-align: justify;&quot;&gt;
&lt;p&gt;The Organization had its beginning as a sub department under the Public Works Department, for Water Supply &amp;amp; Drainage. In 1965, it became a division under the Ministry of Local Government. From 1970, this division functioned as a separate department under the Ministry of Irrigation, Power &amp;amp; Highways and remained so until the present Board was established in January 1975 by an Act of Parliament.&lt;/p&gt;
&lt;p&gt;&lt;br /&gt;The National Water Supply Drainage Board (NWS&amp;amp;DB), which presently functions under the Ministry of Urban Development, Water Supply &amp;amp; Drainage is the principal authority providing safe drinking water and facilitating the provision of sanitation in Sri Lanka. In accordance with the NWS&amp;amp;DB Act, a number of major Urban Water Supply Schemes operated by Local Authorities were taken over by the NWS&amp;amp;DB to provide more coverage and improved service. Consumer metering and billing commenced in 1982. Rural Water Supply &amp;amp; Sanitation programmes including deep well programmes are also being implemented by the NWS&amp;amp;DB.&lt;/p&gt;
&lt;/td&gt;
&lt;td&gt;
&lt;p style=&quot;text-align: center;&quot;&gt;&lt;a href=&quot;http://waterboard.lk/web/images/contents/history.jpg&quot; data-rokbox=&quot;&quot;&gt;&lt;img style=&quot;margin: 10px;&quot; src=&quot;http://waterboard.lk/web/images/contents/history.jpg&quot; alt=&quot;history&quot; width=&quot;300&quot; height=&quot;208&quot; /&gt;&lt;/a&gt;&lt;/p&gt;
&lt;p style=&quot;text-align: center;&quot;&gt;&lt;em&gt;The Hon. Maithripala Senanayake, Ministry of Irrigation Power &amp;amp; Highways at the inaugural meeting of the Board on 02-12-1974&lt;/em&gt;&lt;/p&gt;
&lt;/td&gt;
&lt;/tr&gt;
&lt;tr&gt;
&lt;td colspan=&quot;3&quot;&gt;
&lt;p&gt;During the past years, the organization has considerably expanded its scope of activities.&lt;/p&gt;
&lt;p&gt;&lt;br /&gt;The NWS&amp;amp;DB is presently operating 324 Water Supply Schemes which cover 34% of the total population with pipe borne water supply. 10.5% of the population is served with pipe borne water supply by some Local Authorities, NGO’s and Community Based Organizations. 13% of the population is served with hand pump tube wells. We hope to increase the coverage with pipe borne water to 45.7% by 2015 so that the United Nations Millennium Development Goal of 85% safe drinking water coverage can be achieved by that year. The NWS&amp;amp;DB is also in charge of the sewerage system in Colombo and suburbs, Hantane, Koggala, Hikkaduwa, Kataragama and in few housing schemes.&lt;/p&gt;
&lt;/td&gt;
&lt;/tr&gt;
