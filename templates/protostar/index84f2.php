<h4>Rainwater Harvesting to Mitigate Chronic Kidney Disease in North Central / Western provision</h4>
<p style="text-align: justify;">Chronic Kidney Disease of Unknown Etiology (CKDu) has been prevalent in the North Central and North Western provinces of Sri Lanka over the past decade. The number of cases has been steadily rising since the inception of the disease. In certain areas 5 to 10 % of the population of all age groups including children is affected. CKDu has grown to epidemic level in Kurunegala &amp; Anuradhapura districts. Many research groups including a specialist team of WHO are involved in studies to identify the cause of this disease. Naturally available water in the district is not a major cause of disease according to WHO report (year 2013) but statistics reveal that treated water from NWSDB piped water schemes have very low contribution to it. Considering this situation NWSDB concludes that drinking safe water could control the disease.</p>
<p style="text-align: justify;">As it is not practically possible to cover the entire district with pipe borne water, NWSDB proposes rain water harvesting and provision of treated water via bowser supplies could help the un served communities to avoid this deadly disease. With the lessons learnt from provision of ferro-cement tanks, NWSDB has initiated in year 2010 to provide 154 nos of a capacity of 2000L plastic tanks to identified dwellings of the patients in Polpithigama area. The houses in the respective areas were selected giving weightage for the following.</p>
<ul>
<li>Houses where there is a kidney patient</li>
<li>Houses where there had been a fatality due to CKD</li>
<li>Number of children below 18 years of age</li>
<li>Willing to accept rain water for consumption</li>
</ul>
<p style="text-align: justify;">The Salient features of the facility provided include of a cloth filter, automatic first flush facility and introduction of a smaller capacity poly ethylene tank. The pilot project contains 154 rain water tanks and a follow up monitoring program scheduled to ensure the sustainability of the project.</p>
