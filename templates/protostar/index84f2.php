Demonstration of Treatment Plant
</span></a>
</div>
<div class="accordion-body collapse" id="demonstration-of-treatment-plant">
<div class="accordion-inner panel-body">
<p style="text-align: justify;">Research and Development Section has developed a demonstration model of Water Treatment plant, to understand the basic treatment process in Conventional Water treatment plant.</p>
<p>The processes indicate in this model is given as below.</p>
<p style="text-align: center;"><img src="http://waterboard.lk/web/images/contents/key_sections/research_and_development/basic_treatment_process.jpg" alt="basic treatment process" width="600" height="164" /></p>
<table class="mtable" style="width: 100%;" border="0" cellspacing="1" cellpadding="1">
<tbody>
<tr class="r1" valign="top">
<td><strong>Aeration</strong></td>
<td><strong>:</strong></td>
<td style="text-align: justify;">Water from raw water source, will contain little or no dissolved oxygen. Hence, oxygen is added here. Also this step removes dissolved gases such as CO2, H2S and Ammonia from water thus improving the taste. Also dissolved iron is oxidized</td>
</tr>
<tr class="r2" valign="top">
<td><strong>Coagulation</strong></td>
<td><strong>:</strong></td>
<td style="text-align: justify;">The water contains very small particles less than 1 μm (Colloidal solids). They cannot settle out of suspension naturally because of their smaller size and negative charge. Coagulants such as Alum{[Al2(SO4)3. 18H2O] – Hydrated Aluminium Sulphate} are added to neutralize and destabilize these particles to induce them to aggregate into bundles known as flocs. This flash mixing with Alum is done with correction of pH to around 6.8. Sometimes Lime is used for this pH correction. About 60 seconds is enough for this Flash mixing step.</td>
</tr>
<tr class="r1" valign="top">
<td><strong>Flocculation</strong></td>
<td><strong>:</strong></td>
<td style="text-align: justify;">The bundling of destabilized particles is termed flocculation which is based on particle collisions. Flocculation is done practically in three steps termed “Slow Mixing” which improves the chance of particle collisions.</td>
</tr>
<tr class="r2" valign="top">
<td><strong>Tube Settler</strong></td>
<td><strong>:</strong></td>
<td style="text-align: justify;">The flocs formed after flocculation are settleable and hence removed by settling in sedimentation tank. As the flocs settle, the clarified water at the surface overflows a simple weir on top to the filtration unit. Tube settler is one of the most efficient settling device. It needs only one fifth of land area compared to a plain sedimentation tank.</td>
</tr>
<tr class="r1" valign="top">
<td><strong>Filtration</strong></td>
<td><strong>:</strong></td>
<td style="text-align: justify;">After sedimentation, the remaining impurities in water are very fine solids. (&lt;10 mg/L in concentration) and dissolved material. Some of these impurities are originally in the raw water, but some are formed during the coagulation process. The residual material is removed in the filtration process by passing settled water through a layer of fine sand.</td>
</tr>
<tr class="r2" valign="top">
<td><strong>Disinfection</strong></td>
<td><strong>:</strong></td>
<td style="text-align: justify;">Although the coagulation process can remove some viruses and filtration can remove bacteria up to certain extent, the finished water may still contains some pathogenic viruses and bacteria which need to be destroyed or kept at safe level. Hence chlorination is applied to disinfect the filtered water before distributing to the consumers.</td>
</tr>
</tbody>
