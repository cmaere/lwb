<h4>Provision of Safe Water for Drinking and Cooking Purpose for the Chronic Kidney Disease Affected in Polpithigama</h4>
<p style="text-align: justify;">Chronic Kidney Disease of unknown etiology (CKDu) has been prevalent in the North Western Province of Sri Lanka over the past decade or so. According to Health Ministry, in certain areas of this province fairly large number of population is affected by KDu inclusive of children.</p>
<p style="text-align: justify;">At present, few pockets of the identified CKDu areas are not covered under NWSDB pipe born water supply, but those areas are covered by piped water supply systems provided by NGO’s and CWSSP. These systems supply almost untreated water from groundwater source such as dug wells &amp; tube well.</p>
<p style="text-align: justify;">This study covers the water supply provided by NGOs’ to around 500 families from 30 dug wells, 4 tube wells and one spring.</p>
<p style="text-align: justify;">The aim of this research study is to study the water quality and social survey of the above mentioned 35 groundwater sources. The water quality tests include Physical, Chemical, Biological, Pesticides, Heavy metals, Arsenic and Sodium etc. At present the social survey is in progress.</p>
