<p style="text-align: justify;">In recent past years, National Water Supply &amp; Drainage Board had constructed several package water treatment plants in the capacities of 500, 1000 and 1500 cum per day. Mega plants are most suitable for urban areas where the population is concentrated. Compared with the package water treatment plants the investment for mega plant is very much high. The cost of a 500 cum per day treatment plant only is around SL Rupees 12-13 million according to 2012 calculations, which includes the cost of material, fabrication, transportation &amp; installation from Aerator to Filter exclusive support piers, back wash pumps, chemical dosing, concrete foundations and ancillary buildings etc. The price variation is depends on the site location &amp; the condition.</p>
<p style="text-align: justify;">By the initiative of the Research &amp; Development Section the first 500 cum package plant was constructed in year 2005 in Pugoda. In recent past year with continuous improvements Research &amp; Development section has designed 500, 1000 &amp; 1500 cum treatment plant with tray &amp; cascade aerators with the options of mechanical, hydraulic flocculation. This is a product of team work of NWSDB, the design by the Research &amp; Development Section, drawn by Planning &amp; Design Section, fabrication &amp; installation by Central Work Shop. Finally, the relevant regions take the field adjustments, construction, supply and installation of pumps, chemical dosing and necessary building, supervision, operation &amp; maintenance.</p>
