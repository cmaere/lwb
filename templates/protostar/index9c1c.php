		<summary type="html">&lt;p style=&quot;text-align: justify;&quot;&gt;A Deputy General Manager (DGM) heads the Western North RSC with an Assistant General Manager (AGM) providing support in running the RSC unit. The O&amp;amp;M activities are divided by regions namely the Gampaha Region and the Towns North of Colombo (TNC) region and are managed by two different regional managers. The principal RSC functions are to provide necessary support to the two regional offices in the day to day O&amp;amp;M activities and to provide help and guidance in the ongoing scheme rehabilitation, and augmentation work.&lt;/p&gt;
&lt;p style=&quot;text-align: justify;&quot;&gt;The O&amp;amp;M managers report to the DGM through the AGM and all RSC functions are monitored by the Additional General Manager for Western Zone. The RSC provides services to the regional offices only within their capacity; any additional help as necessary is obtained from the head office (HO) through the respective Additional General Manager.&lt;/p&gt;
