&lt;h4&gt;&lt;strong&gt;The Services rendered by North Western RSC of N.W.S.&amp;amp; D.B. for patients&amp;nbsp;suffering from kidney disease in Kurunegala district&lt;/strong&gt;&lt;/h4&gt;
&lt;p&gt;The data bank of the Department of Health reveals that the no. of CKDu patients in 11 no. of MOH divisions in the Kurunegala district is increasing rapidly. The quality of drinking water could be a major component for the increase of CKDu patients. Hence the N.W.S.&amp;amp; D.B. has initiated no. of projects in these areas.&lt;br /&gt;• In the Polpithigama and Maho MOH divisions, 252 kidney patients have been provided with 250 no.s of Rain water collecting tanks with capacity of 2000 L. &lt;br /&gt;• 25 no.s selected schools in the above areas have been provided with 41 Rain water collecting tanks with capacity of 5000 L.&lt;br /&gt;• Work has commenced for restoring hand pumps and extending the distribution line to supply water from the Maho WSS to these affected areas.&lt;br /&gt;• In addition to this we have lounged a joint programme along with the DHS office in Kurunegala to monitor the quality of drinking water sources (Dug wells) of these affected areas namely Polpithigama, Giribawa and Galgamuwa.&lt;/p&gt;
&lt;p&gt;&amp;nbsp;&lt;/p&gt;
&lt;p&gt;</div></div></div></div>&lt;/p&gt;
&lt;p&gt;&amp;nbsp;&lt;/p&gt;</summary>
		<content type="html">&lt;p style=&quot;text-align: justify;&quot;&gt;Under the present setup of RSC (North Western), both Deputy General Manager (DGM) and Assistant General Manager (AGM) look after the O&amp;amp;M functions in addition to their development functions. The Manager (O&amp;amp;M) usually coordinates with the AGM for his normal O&amp;amp;M functions but in the case of development functions like rehabilitation, he involves the DGM. In addition budgetary controls are also being managed through the DGM’s office.&lt;/p&gt;
&lt;p style=&quot;text-align: justify;&quot;&gt;The District Engineer (DE) Kurunegala office is located within the same premises as the Manager (O&amp;amp;M) office as a result of which the independence and identity of DE’s office Kurunegala is in question. Most of the coordination and consumer relation functions are commonly handled at both offices but in some instances customers tend to bypass the DE and directly access the Manager (O&amp;amp;M). In contrast, the DE office Puttalam which is located in Chilaw which is far away from the Manager (O&amp;amp;M) Kurunegala office appears to be functioning better. A major portion of the service connections are now in Puttalam district compared to Kurunegala district which has developed only a limited number of water supply systems in the past.&lt;/p&gt;
