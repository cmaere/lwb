&lt;h4&gt;&lt;strong&gt;The Services rendered by North Western RSC of N.W.S.&amp;amp; D.B. for patients&amp;nbsp;suffering from kidney disease in Kurunegala district&lt;/strong&gt;&lt;/h4&gt;
&lt;p&gt;The data bank of the Department of Health reveals that the no. of CKDu patients in 11 no. of MOH divisions in the Kurunegala district is increasing rapidly. The quality of drinking water could be a major component for the increase of CKDu patients. Hence the N.W.S.&amp;amp; D.B. has initiated no. of projects in these areas.&lt;br /&gt;• In the Polpithigama and Maho MOH divisions, 252 kidney patients have been provided with 250 no.s of Rain water collecting tanks with capacity of 2000 L. &lt;br /&gt;• 25 no.s selected schools in the above areas have been provided with 41 Rain water collecting tanks with capacity of 5000 L.&lt;br /&gt;• Work has commenced for restoring hand pumps and extending the distribution line to supply water from the Maho WSS to these affected areas.&lt;br /&gt;• In addition to this we have lounged a joint programme along with the DHS office in Kurunegala to monitor the quality of drinking water sources (Dug wells) of these affected areas namely Polpithigama, Giribawa and Galgamuwa.&lt;/p&gt;
