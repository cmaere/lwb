		<summary type="html">&lt;p style=&quot;text-align: justify;&quot;&gt;Under the present setup of RSC (North Western), both Deputy General Manager (DGM) and Assistant General Manager (AGM) look after the O&amp;amp;M functions in addition to their development functions. The Manager (O&amp;amp;M) usually coordinates with the AGM for his normal O&amp;amp;M functions but in the case of development functions like rehabilitation, he involves the DGM. In addition budgetary controls are also being managed through the DGM’s office.&lt;/p&gt;
&lt;p style=&quot;text-align: justify;&quot;&gt;The District Engineer (DE) Kurunegala office is located within the same premises as the Manager (O&amp;amp;M) office as a result of which the independence and identity of DE’s office Kurunegala is in question. Most of the coordination and consumer relation functions are commonly handled at both offices but in some instances customers tend to bypass the DE and directly access the Manager (O&amp;amp;M). In contrast, the DE office Puttalam which is located in Chilaw which is far away from the Manager (O&amp;amp;M) Kurunegala office appears to be functioning better. A major portion of the service connections are now in Puttalam district compared to Kurunegala district which has developed only a limited number of water supply systems in the past.&lt;/p&gt;
