&lt;h4&gt;National Policy on Private Sector Participation In Water Supply and Sanitation&lt;/h4&gt;
&lt;p&gt;

<div class="nn_sliders accordion panel-group" id="set-nn_sliders-2">
<div class="accordion-group panel">
<div class="accordion-heading panel-heading">
<a class="accordion-toggle" data-toggle="collapse" data-parent="#set-nn_sliders-2" href="http://waterboard.lk/web/index.php?option=com_content&view=category&id=33&Itemid=103&lang=en&format=feed&type=atom#objective"><div class="clo"></div><div class="ope"></div><span class="nn_sliders-toggle-inner">
Objective
</span></a>
</div>
<div class="accordion-body collapse" id="objective">
<div class="accordion-inner panel-body">&lt;/p&gt;
&lt;p&gt;The government has announced a national target: &quot;Safe water for all by 2010&quot;&lt;/p&gt;
&lt;p&gt;The targets set are as follows:&lt;/p&gt;
&lt;ul&gt;
&lt;li style=&quot;text-align: justify;&quot;&gt;Increase coverage to provide access to safe water to 95% of the population by the year 2010 and at an affordable price.&lt;/li&gt;
&lt;li style=&quot;text-align: justify;&quot;&gt;To achieve an adequate level of pipe borne water supply in urban areas,&lt;/li&gt;
&lt;li style=&quot;text-align: justify;&quot;&gt;To meet the demand for 24 hour water supply from industry and service sectors.&lt;/li&gt;
&lt;/ul&gt;
&lt;p&gt;

</div></div></div>
<div class="accordion-group panel">
<div class="accordion-heading panel-heading">
<a class="accordion-toggle" data-toggle="collapse" data-parent="#set-nn_sliders-2" href="http://waterboard.lk/web/index.php?option=com_content&view=category&id=33&Itemid=103&lang=en&format=feed&type=atom#back-ground"><div class="clo"></div><div class="ope"></div><span class="nn_sliders-toggle-inner">
Back ground
</span></a>
</div>
<div class="accordion-body collapse" id="back-ground">
<div class="accordion-inner panel-body">&lt;/p&gt;
&lt;p style=&quot;text-align: justify;&quot;&gt;The 1994 Demographic Survey of the department Census and statistics reflects that 72% of the populations have access to safe drinking water through protected sources. With the rapid increase in urbanization and standard of living, the demand for quality water supply is estimated to increase at around 8-10% per annum. Very few of the water systems in place have the capacity to provide 24 hour supply of water which is desired service level.&lt;/p&gt;
&lt;p style=&quot;text-align: justify;&quot;&gt;Industrial and service organizations consume a large amount of water and are important growth areas which generate income and employment in the surrounding area. A 24 hour water supply is needed for such commercial activity.&lt;/p&gt;
&lt;p style=&quot;text-align: justify;&quot;&gt;Safe drinking water and adequate provision for dealing with waste water is an important public health concern. Urbanization without adequate water supply systems and waste water collection and disposal facilities leads to contamination of water supply. Lack of adequate urban development.&lt;/p&gt;
&lt;p&gt;

</div></div></div>
<div class="accordion-group panel">
<div class="accordion-heading panel-heading">
<a class="accordion-toggle" data-toggle="collapse" data-parent="#set-nn_sliders-2" href="http://waterboard.lk/web/index.php?option=com_content&view=category&id=33&Itemid=103&lang=en&format=feed&type=atom#investment"><div class="clo"></div><div class="ope"></div><span class="nn_sliders-toggle-inner">
Investment
</span></a>
</div>
<div class="accordion-body collapse" id="investment">
<div class="accordion-inner panel-body">&lt;/p&gt;
&lt;p style=&quot;text-align: justify;&quot;&gt;The fund needed in the water sector to the year 2010 has been estimated by the National Water Supply and Drainage Board to amount to Rest. 85 billion. The funding available for the water sector from central government over the next 10 years amount to a total of Rest. 45 billion due to constrains placed by the capacity for public borrowing. This is around half of the funds needed for the sector.&lt;/p&gt;
&lt;p style=&quot;text-align: justify;&quot;&gt;Increasingly, multilateral donors are making long term funds available to the private sector to project finance investment in infrastructure. In order to meet the investment gap and also to attract long term multilateral funds designated to the private section into Sri Lanka, private sector investment in water supply is invited.&lt;/p&gt;
&lt;h5 style=&quot;text-align: justify;&quot;&gt;Public - Private Partnership&lt;/h5&gt;
&lt;p style=&quot;text-align: justify;&quot;&gt;Private sector is invited to enter into partnership with the government to operate, maintain and extend water systems. This Public Private Partnership can take many forms including the following listed below.&lt;/p&gt;
&lt;ul&gt;
&lt;li style=&quot;text-align: justify;&quot;&gt;&lt;strong&gt;Service Contracts:&lt;/strong&gt;&lt;br /&gt;Where the public authority retains responsibility for operation and maintenance of the system and limited scope services are contracted out or out sourced with payments to the contractor being linked to performance targets.&lt;br /&gt;&lt;br /&gt;&lt;/li&gt;
&lt;li style=&quot;text-align: justify;&quot;&gt;&lt;strong&gt;Management Contracts:&lt;/strong&gt;&lt;br /&gt;Where the public authority transfers to a private company the responsibility of the entire operation and maintenance of a system. The public authority will undertake capital investment while payments to the operator will be based on a fixed fee and incentives for increasing efficiency.&lt;br /&gt;&lt;br /&gt;&lt;/li&gt;
&lt;li style=&quot;text-align: justify;&quot;&gt;&lt;strong&gt;Lease/ Concession Contracts:&lt;/strong&gt;&lt;br /&gt;Where the private operator rents the facilities from the public authority and is responsible for the operations and maintenance including investing in the system on an agreed basis to ensure desired coverage. The operator needs to be ensured of a reasonable return on investment and should be incentives to increase efficiency.&lt;br /&gt;&lt;br /&gt;It is expected that all major fixed assets presently belonging to the water schemes would remain with the government. As private investment in water supply schemes would need to establish an adequate -rate of return on the project, it is anticipated that government resources will be targeted to make water affordable and increase accessibility including by providing government subsidy to cushion tariff levels.&lt;br /&gt;&lt;br /&gt;The most suitable model to be adopted will have to be assessed on a case by case basis based on coverage objectives and acceptable levels of tariff so that resources available with the government are used in the most appropriate manner.&lt;br /&gt;&lt;br /&gt;Private sector will be invited into partnership through transparent, open competitive biding procedures.&lt;/li&gt;
&lt;/ul&gt;
