<div class="accordion-body collapse" id="objective-2">
<div class="accordion-inner panel-body">&lt;/p&gt;
&lt;p&gt;The government has announced a national target: &quot;Safe water for all by 2010&quot;&lt;/p&gt;
&lt;p&gt;The targets set are as follows:&lt;/p&gt;
&lt;ul&gt;
&lt;li style=&quot;text-align: justify;&quot;&gt;Increase coverage to provide access to safe water to 95% of the population by the year 2010 and at an affordable price.&lt;/li&gt;
