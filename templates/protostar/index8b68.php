&lt;h4&gt;National Policy on Water Supply and Sanitation – August 2002&lt;/h4&gt;
&lt;p&gt;

<div class="nn_sliders accordion panel-group" id="set-nn_sliders-1">
<div class="accordion-group panel">
<div class="accordion-heading panel-heading">
<a class="accordion-toggle" data-toggle="collapse" data-parent="#set-nn_sliders-1" href="http://waterboard.lk/web/index.php?option=com_content&view=category&id=33&Itemid=103&lang=en&format=feed&type=atom#introduction"><div class="clo"></div><div class="ope"></div><span class="nn_sliders-toggle-inner">
Introduction
</span></a>
</div>
<div class="accordion-body collapse" id="introduction">
<div class="accordion-inner panel-body">&lt;/p&gt;
&lt;p style=&quot;text-align: justify;&quot;&gt;Providing safe drinking water and access to sanitation services is an essential or fundamental element of the Government's program for the economic and social development of Sri Lanka. While coverage levels and service quality have improved markedly over the past decade, the need for water services has outstripped the government's ability to provide sufficient water and sanitation (including pipe borne sewerage infrastructure) and ensure equitable access to the citizens throughout the country. As such, the GoSL has initiated a program for sector reform. Key steps that are being undertaken as part of the reform process include: establishing a regulatory commission for water supply and sewerage and contracting private operators in selected regions of the country to improve operational efficiency and provide private sector investment finance. Further, GoSL has established a separate division under the line ministry for Rural Water Supply &amp;amp; Sanitation sub sector.&lt;/p&gt;
&lt;p style=&quot;text-align: justify;&quot;&gt;To guide the growth of the water and sanitation sector, the Ministry of Housing and Plantation Infrastructures has developed an overarching policy framework for the sector. This policy provides guidance for government agencies including provincial councils &amp;amp; local authorities, lending institutions, and other community based organizations, non governmental organizations involved in water supply and sanitation services in the design and implementation of programs and investment strategies to achieve the coverage, service quality, and cost recovery objectives of the GoSL.&lt;/p&gt;
&lt;p style=&quot;text-align: justify;&quot;&gt;This statement reflects the various policies that have been previously developed for the sector by the Ministry of Housing and Plantation Infrastructure the National Water Supply &amp;amp; Drainage Board and other Ministries and government agencies. It will be updated periodically by the Ministry of Housing and Plantation Infrastructure to facilitate achieving the goals of GoSL for the sector.&lt;/p&gt;
&lt;p&gt;

</div></div></div>
<div class="accordion-group panel">
<div class="accordion-heading panel-heading">
<a class="accordion-toggle" data-toggle="collapse" data-parent="#set-nn_sliders-1" href="http://waterboard.lk/web/index.php?option=com_content&view=category&id=33&Itemid=103&lang=en&format=feed&type=atom#sector-vision-and-goals"><div class="clo"></div><div class="ope"></div><span class="nn_sliders-toggle-inner">
Sector Vision and Goals
</span></a>
</div>
<div class="accordion-body collapse" id="sector-vision-and-goals">
<div class="accordion-inner panel-body">&lt;/p&gt;
&lt;h5&gt;Sector Vision&lt;/h5&gt;
&lt;p style=&quot;text-align: justify;&quot;&gt;The Government of Sri Lanka is committed to improving the standard of living, promoting economic prosperity, and preserving the environment by providing access to safe drinking water and adequate sanitation facilities to the people of Sri Lanka.&lt;/p&gt;
&lt;h5&gt;Goals in water supply&lt;/h5&gt;
&lt;ul&gt;
&lt;li style=&quot;text-align: justify;&quot;&gt;Access to sufficient and safe drinking water is provided to 85% of the population of Sri Lanka by 2010 and 100% by 2025;&lt;/li&gt;
&lt;li style=&quot;text-align: justify;&quot;&gt;Piped water supply is provided to 100% of the urban population and ….% of the rural population by 2010; and&lt;/li&gt;
&lt;li style=&quot;text-align: justify;&quot;&gt;Service levels and the quality of water achieves national standards in urban and rural areas.&lt;/li&gt;
&lt;/ul&gt;
&lt;p style=&quot;text-align: justify;&quot;&gt;This policy statement covers the provision of drinking water from bulk water supply to the final distribution to the consumers through piped networks and other means such as tankers, tube and dug wells, and other community distribution systems. For sanitation it covers the collection of sewerage through piped systems, community-based sanitation facilities, on-site sanitation facilities, and the treatment of wastewater for discharge into the environment.&lt;/p&gt;
&lt;p style=&quot;text-align: justify;&quot;&gt;These policies are listed at the end of this document.&lt;/p&gt;
&lt;h5&gt;Goals in sanitation&lt;/h5&gt;
&lt;ul&gt;
&lt;li style=&quot;text-align: justify;&quot;&gt;Access to adequate sanitation is available to 70% of the population of Sri Lanka by 2010 and 100% by 2025;&lt;/li&gt;
&lt;li style=&quot;text-align: justify;&quot;&gt;Piped sewerage systems are provided in the major urban areas and selected growth centres; and&lt;/li&gt;
&lt;li style=&quot;text-align: justify;&quot;&gt;Standard on-site sanitation is available to all those not connected to a sewerage system or other sanitation scheme.&lt;/li&gt;
&lt;/ul&gt;
&lt;p&gt;

