&lt;p style=&quot;text-align: justify;&quot;&gt;Cost Recovery objective for Urban Water Supply is to meet through revenue from tariffs, O&amp;amp;M costs, overheads and debt service or depreciation which ever is greater. Similarly Tariff setting shall address the demand management thereby reducing the stress on water resources and less contribution to waste generation.&lt;/p&gt;
&lt;p style=&quot;text-align: justify;&quot;&gt;Private sector involvement is encouraged in according with Government policies and within a regulatory frame work which will improve the management efficiently.&lt;/p&gt;
&lt;p style=&quot;text-align: justify;&quot;&gt;Investment for distribution systems through active financial involvement by the beneficiaries.&lt;/p&gt;
&lt;p style=&quot;text-align: justify;&quot;&gt;Loan/ Grant ratios shall be based on current levels with in urban areas.&lt;/p&gt;
&lt;h5&gt;Private Sector Participation&lt;/h5&gt;
&lt;p style=&quot;text-align: justify;&quot;&gt;Involvement of Private sector in the management and investments of water utilities has been identified as a key strategy to overcome the present day constraints caused by rapid increase in demand for adequate and reliable pipe-borne water supply.&lt;/p&gt;
&lt;p style=&quot;text-align: justify;&quot;&gt;The national monopoly has to give way to open competition, and encouragement will be given to the participation of the private sector to attract needy investments.&lt;/p&gt;
