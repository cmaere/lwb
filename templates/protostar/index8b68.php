<div class="accordion-group panel">
<div class="accordion-heading panel-heading">
<a class="accordion-toggle" data-toggle="collapse" data-parent="#set-nn_sliders-4" href="http://waterboard.lk/web/index.php?option=com_content&view=category&id=33&Itemid=103&lang=en&format=feed&type=atom#introduction-3"><div class="clo"></div><div class="ope"></div><span class="nn_sliders-toggle-inner">
Introduction
</span></a>
</div>
<div class="accordion-body collapse" id="introduction-3">
<div class="accordion-inner panel-body">&lt;/p&gt;
&lt;p style=&quot;text-align: justify;&quot;&gt;Providing safe drinking water and access to sanitation services is an essential or fundamental element of the Government's program for the economic and social development of Sri Lanka. While coverage levels and service quality have improved markedly over the past decade, the need for water services has outstripped the government's ability to provide sufficient water and sanitation (including pipe borne sewerage infrastructure) and ensure equitable access to the citizens throughout the country. As such, the GoSL has initiated a program for sector reform. Key steps that are being undertaken as part of the reform process include: establishing a regulatory commission for water supply and sewerage and contracting private operators in selected regions of the country to improve operational efficiency and provide private sector investment finance. Further, GoSL has established a separate division under the line ministry for Rural Water Supply &amp;amp; Sanitation sub sector.&lt;/p&gt;
&lt;p style=&quot;text-align: justify;&quot;&gt;To guide the growth of the water and sanitation sector, the Ministry of Housing and Plantation Infrastructures has developed an overarching policy framework for the sector. This policy provides guidance for government agencies including provincial councils &amp;amp; local authorities, lending institutions, and other community based organizations, non governmental organizations involved in water supply and sanitation services in the design and implementation of programs and investment strategies to achieve the coverage, service quality, and cost recovery objectives of the GoSL.&lt;/p&gt;
&lt;p style=&quot;text-align: justify;&quot;&gt;This statement reflects the various policies that have been previously developed for the sector by the Ministry of Housing and Plantation Infrastructure the National Water Supply &amp;amp; Drainage Board and other Ministries and government agencies. It will be updated periodically by the Ministry of Housing and Plantation Infrastructure to facilitate achieving the goals of GoSL for the sector.&lt;/p&gt;
