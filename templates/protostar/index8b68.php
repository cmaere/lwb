&lt;h4 style=&quot;text-align: justify;&quot;&gt;Primary Functions&lt;/h4&gt;
&lt;ul&gt;
&lt;li style=&quot;text-align: justify;&quot;&gt;Investigation, planning, design and construction supervision of water supply and sewerage projects with local funds and donor assistance. Carry out feasibility studies, cost estimation &amp;amp; Environmental Impact Assessment for such projects&lt;/li&gt;
&lt;li style=&quot;text-align: justify;&quot;&gt;Operation and maintenance of water supply and sewerage schemes to provide satisfactory service to customers&lt;/li&gt;
&lt;li style=&quot;text-align: justify;&quot;&gt;Billing and collection through affordable tariff setting&lt;/li&gt;
&lt;/ul&gt;
&lt;h4&gt;Secondary Functions&lt;/h4&gt;
&lt;ul&gt;
&lt;li style=&quot;text-align: justify;&quot;&gt;Human Resource planning and development&lt;/li&gt;
&lt;li style=&quot;text-align: justify;&quot;&gt;Research and development for service improvement utilizing innovative techniques&lt;/li&gt;
&lt;li style=&quot;text-align: justify;&quot;&gt;Budgeting and financial control&lt;/li&gt;
&lt;li style=&quot;text-align: justify;&quot;&gt;Publicity and consumer awareness programmes on effective use of water and reduction of non-revenue water&lt;/li&gt;
&lt;li style=&quot;text-align: justify;&quot;&gt;Corporate Planning and Strategic Management&lt;/li&gt;
&lt;li style=&quot;text-align: justify;&quot;&gt;Laboratory services for water and wastewater quality monitoring and control&lt;/li&gt;
&lt;li style=&quot;text-align: justify;&quot;&gt;Technical assistance to Community Based Organizations (CBO’s), Local Authorities, State Agencies and Private/ Public sector institutions&lt;/li&gt;
&lt;li style=&quot;text-align: justify;&quot;&gt;Ground water investigations, deep drilling, Tube Well / Hand Pump installation&lt;/li&gt;
&lt;li style=&quot;text-align: justify;&quot;&gt;Consultancy services on water supply and sanitation&lt;/li&gt;
&lt;li style=&quot;text-align: justify;&quot;&gt;Project formulation and management&lt;/li&gt;
&lt;li style=&quot;text-align: justify;&quot;&gt;Coordination with sector actors and stakeholders&lt;/li&gt;
