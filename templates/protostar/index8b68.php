&lt;h4&gt;Urban Water Supply Policy&lt;/h4&gt;
&lt;p&gt;

<div class="nn_sliders accordion panel-group" id="set-nn_sliders-3">
<div class="accordion-group panel">
<div class="accordion-heading panel-heading">
<a class="accordion-toggle" data-toggle="collapse" data-parent="#set-nn_sliders-3" href="http://waterboard.lk/web/index.php?option=com_content&view=category&id=33&Itemid=103&lang=en&format=feed&type=atom#introduction-2"><div class="clo"></div><div class="ope"></div><span class="nn_sliders-toggle-inner">
Introduction
</span></a>
</div>
<div class="accordion-body collapse" id="introduction-2">
<div class="accordion-inner panel-body">&lt;/p&gt;
&lt;h5&gt;General&lt;/h5&gt;
&lt;p style=&quot;text-align: justify;&quot;&gt;The present urban population in Sri Lanka is around 5.7 million. It is expected that this will increase to 5.8 million, 9.5 million &amp;amp; 15 million by the years 2000, 2015 and 2030. Accordingly the corresponding urban population will reach 31.5%, 45%, &amp;amp; 65% in the respective years. (Source Presidential Task Force Report on House and Urban Development).&lt;/p&gt;
&lt;p style=&quot;text-align: justify;&quot;&gt;To cover the urban population and satisfy the demand of economic sectors such as Industry, commerce, shipping, tourism and also to improve the existing facilities to provide satisfactory level of service to the existing customers a National Policy for Urban water has become priority.&lt;/p&gt;
&lt;p style=&quot;text-align: justify;&quot;&gt;The policy described herein briefly summarizes the present status, future goals and objectives, selection criteria, investment strategies and implementation methods including the operation and maintenance scenarios to achieve the National requirement of Urban Water.&lt;/p&gt;
&lt;p style=&quot;text-align: justify;&quot;&gt;Further more this policy is in accordance with vision and mission of the National Water Supply and Drainage Board's plan.&lt;/p&gt;
&lt;h5&gt;Status of Urban Water Supply&lt;/h5&gt;
&lt;h6&gt;Present Position of water supply coverage&lt;/h6&gt;
&lt;p style=&quot;text-align: justify;&quot;&gt;The total population in Sri Lanka was 18.60 million in the year 1998. Out of this 5.6 million covers the urban area which amounts to 29%. At present 90% of this population has access to safe drinking water where 67% is provided with pipe borne water supplies. Out of the piped schemes maintained by NWSDB only 36% has the capacity to provide 24 hrs supply. Most of the other schemes have an average of 12 hrs supply.&lt;/p&gt;
&lt;h6&gt;Future Goals &amp;amp; Objectives&lt;/h6&gt;
&lt;p style=&quot;text-align: justify;&quot;&gt;It is expected to cover the total urban population with access to safe drinking water by year 2010. Urban population is expected to reach 8.85 million in the year 2010 against a total population of 22.13 million. Service level improvements in terms of accessibility, duration of supply and quality of water supplied are the other major objectives.&lt;/p&gt;
&lt;p&gt;

</div></div></div>
<div class="accordion-group panel">
<div class="accordion-heading panel-heading">
<a class="accordion-toggle" data-toggle="collapse" data-parent="#set-nn_sliders-3" href="http://waterboard.lk/web/index.php?option=com_content&view=category&id=33&Itemid=103&lang=en&format=feed&type=atom#the-policy-on-urban-water-supply"><div class="clo"></div><div class="ope"></div><span class="nn_sliders-toggle-inner">
The Policy on Urban Water Supply
</span></a>
