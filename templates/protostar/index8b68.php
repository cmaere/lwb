&lt;h5&gt;Non Integrated Urban Water Supply&lt;/h5&gt;
&lt;p style=&quot;text-align: justify;&quot;&gt;The NWSDB will continue to develop and support non Integrated urban water supply schemes which are small and capital intensive and also provide services for development of rural water supply.&lt;/p&gt;
&lt;h5&gt;Rural Water Supply&lt;/h5&gt;
&lt;p style=&quot;text-align: justify;&quot;&gt;Local authorities, the community and small entrepreneurs will be encouraged to take over the management of small rural water schemes with the technical support from the NWSDB.&lt;/p&gt;
&lt;h5&gt;Head Works and Bulk Selling&lt;/h5&gt;
&lt;p style=&quot;text-align: justify;&quot;&gt;Development of head works and bulk selling by private developers to operators is an area where private investment is invited. These investment can take the from of BOOT or reverse BOOT projects. Under the BOOT arrangement, the private sector builds, finances, owns and operates the facility and eventually transfers ownership to the government. Under reverse BOOT, the facility is financed and built by the government and the private firm operates the facility and purchases it by payment of an annual fee over time.&lt;/p&gt;
&lt;h5&gt;Waste Water Systems&lt;/h5&gt;
&lt;p style=&quot;text-align: justify;&quot;&gt;Improvement of the waste water systems essential to meet the environmental needs of urbanization and industrialization. Waste water systems are more capital intensive than water supply systems will continue to be provided by the government in the medium term. During this period the government will move towards charging for operation and maintenance of waste water systems from the consumers with a view to providing a better service. Private sector will be invited to manage the operation and maintenance of sewerage systems.&lt;/p&gt;
