&lt;ul&gt;
&lt;li&gt;Set tariffs based on government policy and service cost levels,&lt;/li&gt;
&lt;li&gt;Ensure that operators carry out their legal responsibility under contracts with the State.&lt;/li&gt;
&lt;li&gt;Insist of standard of quality and service with appropriate penalties.&lt;/li&gt;
&lt;li&gt;Promote high standards of efficiency.&lt;/li&gt;
&lt;li&gt;Deal with complaints and settle disputes.&lt;/li&gt;
&lt;/ul&gt;
&lt;p style=&quot;text-align: justify;&quot;&gt;The regulator will have jurisdiction over both public and private providers of water.&lt;/p&gt;
