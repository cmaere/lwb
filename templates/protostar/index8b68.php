&lt;p style=&quot;text-align: justify;&quot;&gt;A dependable source of safe water is valued highly by most households. The affordability for water is estimated on average to be a maximum of 5% of income. Higher levels of consumption can be charged at progressively higher rates within a formula set for tariff adjustments. This formula should inter - alia be based the premium charge to the commercial sector over the medium term by proving competitive tariff.&lt;/p&gt;
