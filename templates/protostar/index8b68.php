&lt;p&gt;The Urban water supply would be defined as Water supply,&lt;/p&gt;
&lt;ul&gt;
&lt;li&gt;Within Greater Colombo&lt;/li&gt;
&lt;li&gt;Within Urban and Municipal Council administrations&lt;/li&gt;
&lt;li&gt;Within newly designated areas by the Ministry of National planning.&lt;/li&gt;
&lt;li&gt;Where the adopted technology is complex.&lt;/li&gt;
&lt;/ul&gt;
&lt;h5&gt;Investment Selection Criteria.&lt;/h5&gt;
&lt;p style=&quot;text-align: justify;&quot;&gt;A selection criteria shall be developed in order to prioritize the ranking of various projects. These criteria shall primarily address the priority of districts then the division of urban centers based on major parameters like population density, per capita investment, subsidy ration pertaining to the area ect. Similarly a division is necessary between new schemes and augmentation of existing schemes.&lt;/p&gt;
&lt;p&gt;Following factors shall be considered in the need assessment study.&lt;/p&gt;
