Back ground
</span></a>
</div>
<div class="accordion-body collapse" id="back-ground-2">
<div class="accordion-inner panel-body">&lt;/p&gt;
&lt;p style=&quot;text-align: justify;&quot;&gt;The 1994 Demographic Survey of the department Census and statistics reflects that 72% of the populations have access to safe drinking water through protected sources. With the rapid increase in urbanization and standard of living, the demand for quality water supply is estimated to increase at around 8-10% per annum. Very few of the water systems in place have the capacity to provide 24 hour supply of water which is desired service level.&lt;/p&gt;
&lt;p style=&quot;text-align: justify;&quot;&gt;Industrial and service organizations consume a large amount of water and are important growth areas which generate income and employment in the surrounding area. A 24 hour water supply is needed for such commercial activity.&lt;/p&gt;
&lt;p style=&quot;text-align: justify;&quot;&gt;Safe drinking water and adequate provision for dealing with waste water is an important public health concern. Urbanization without adequate water supply systems and waste water collection and disposal facilities leads to contamination of water supply. Lack of adequate urban development.&lt;/p&gt;
&lt;p&gt;

