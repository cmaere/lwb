<a class="accordion-toggle" data-toggle="collapse" data-parent="#set-nn_sliders-1" href="http://waterboard.lk/web/index.php?option=com_content&view=category&id=33&Itemid=103&lang=en&format=feed&type=atom#sector-objectives-and-strategies"><div class="clo"></div><div class="ope"></div><span class="nn_sliders-toggle-inner">
Sector Objectives and Strategies
</span></a>
</div>
<div class="accordion-body collapse" id="sector-objectives-and-strategies">
<div class="accordion-inner panel-body">&lt;/p&gt;
&lt;table&gt;
&lt;tbody&gt;
&lt;tr class=&quot;mhead&quot;&gt;
&lt;td colspan=&quot;3&quot;&gt;Sector Structure&lt;/td&gt;
&lt;/tr&gt;
&lt;tr class=&quot;r1&quot; valign=&quot;top&quot;&gt;
&lt;td&gt;&lt;strong&gt;Objective&lt;/strong&gt;&lt;/td&gt;
&lt;td&gt;&lt;strong&gt;:&lt;/strong&gt;&lt;/td&gt;
&lt;td style=&quot;text-align: justify;&quot;&gt;Reform the structure for service provision to provide incentives for efficiency, attract private sector participation , improve accountability, and support community involvement while ensuring adequate provision for low-income urban and rural consumers&lt;/td&gt;
&lt;/tr&gt;
&lt;tr class=&quot;r2&quot; valign=&quot;top&quot;&gt;
&lt;td&gt;&lt;strong&gt;Strategies&lt;/strong&gt;&lt;/td&gt;
&lt;td&gt;&lt;strong&gt;:&lt;/strong&gt;&lt;/td&gt;
&lt;td&gt;&lt;ol style=&quot;list-style-type: lower-alpha;&quot;&gt;
&lt;li style=&quot;text-align: justify;&quot;&gt;Where the private sector is involved in providing water services, the Government, NWS&amp;amp;DB, Municipal Councils, Urban Councils, and Pradeshiya Sabhas shall retain ownership of water supply and sewerage assets unless they are privately financed and developed.&lt;/li&gt;
&lt;li style=&quot;text-align: justify;&quot;&gt;The involvement of the private sector in water supply and sewerage services shall be sought to improve the efficiency and autonomy of service providers and to facilitate private sector investment ,from both international and national investors, in water services.&lt;/li&gt;
&lt;li style=&quot;text-align: justify;&quot;&gt;Water supply and sewerage systems shall be integrated to the extent possible and managed for cost effective service delivery.&lt;/li&gt;
&lt;li style=&quot;text-align: justify;&quot;&gt;The planning functions of NWSDB shall be separated from the operational functions and separate regional cost centers shall be established within NWSDB to promote accountability and provide incentives for increased operational efficiency.&lt;/li&gt;
&lt;li style=&quot;text-align: justify;&quot;&gt;Local authorities, the CBO'S and community groups and small entrepreneurs shall be encouraged to take over the management of rural water schemes.&lt;/li&gt;
&lt;li style=&quot;text-align: justify;&quot;&gt;The NWSDB shall assist in the provision of management and technical support for the development and operations of small non-integrated urban water supply schemes and rural schemes on a cost recovery basis.&lt;/li&gt;
&lt;/ol&gt;&lt;/td&gt;
&lt;/tr&gt;
&lt;tr class=&quot;mhead&quot;&gt;
&lt;td colspan=&quot;3&quot;&gt;Institutional and Regulatory Structure&lt;/td&gt;
&lt;/tr&gt;
&lt;tr class=&quot;r1&quot; valign=&quot;top&quot;&gt;
&lt;td&gt;&lt;strong&gt;Objective&lt;/strong&gt;&lt;/td&gt;
&lt;td&gt;&lt;strong&gt;:&lt;/strong&gt;&lt;/td&gt;
&lt;td style=&quot;text-align: justify;&quot;&gt;Bring the water sector under the regulatory and institutional framework of public utilities by setting out clear roles and responsibilities among government agencies and service providers that creates a transparent, fair and stable regulatory environment that is based on clear principles and procedures.&lt;/td&gt;
&lt;/tr&gt;
&lt;tr class=&quot;r2&quot; valign=&quot;top&quot;&gt;
&lt;td&gt;&lt;strong&gt;Strategies&lt;/strong&gt;&lt;/td&gt;
&lt;td&gt;&lt;strong&gt;:&lt;/strong&gt;&lt;/td&gt;
&lt;td&gt;&lt;ol style=&quot;list-style-type: lower-alpha;&quot;&gt;
&lt;li style=&quot;text-align: justify;&quot;&gt;The respective roles and responsibilities for policy making, planning, regulation, and the provision of services shall be clarified and effective coordination mechanisms among the responsible entities established.&lt;/li&gt;
&lt;li style=&quot;text-align: justify;&quot;&gt;Policy-making functions for the sector shall be developed by the line ministry in consultation with the Provincial Councils.&lt;/li&gt;
&lt;li style=&quot;text-align: justify;&quot;&gt;Supervisory and facilitation functions for the provision of rural services shall be devolved to the Provincial Councils while responsibility for planning and service provision shall be the responsibility of local authorities and CBOs.&lt;/li&gt;
&lt;li style=&quot;text-align: justify;&quot;&gt;Regulate tariffs, service quality, water quality, and consumer protection, in compliance with an industry specific act,for both public and private sector service providers in urban and rural areas.&lt;/li&gt;
&lt;li style=&quot;text-align: justify;&quot;&gt;The regulatory framework shall ensure consistency and equity in the regulation of all service providers.&lt;/li&gt;
&lt;li style=&quot;text-align: justify;&quot;&gt;Sector policy and planning activities at all levels of government shall be based on a participatory approach that involves consumers in the decision-making process.&lt;/li&gt;
&lt;/ol&gt;&lt;/td&gt;
&lt;/tr&gt;
&lt;tr class=&quot;mhead&quot;&gt;
&lt;td colspan=&quot;3&quot;&gt;Tariffs, Operational Costs and Subsidies&lt;/td&gt;
&lt;/tr&gt;
&lt;tr class=&quot;r1&quot; valign=&quot;top&quot;&gt;
&lt;td&gt;&lt;strong&gt;Objective&lt;/strong&gt;&lt;/td&gt;
&lt;td&gt;&lt;strong&gt;:&lt;/strong&gt;&lt;/td&gt;
&lt;td style=&quot;text-align: justify;&quot;&gt;Sustain the service delivery of the water and sanitation sector by gradually increasing tariffs to reflect the full cost of efficient service and reduce subsidies while ensuring the affordability of water and sanitation for low-income urban and rural consumers.&lt;/td&gt;
&lt;/tr&gt;
&lt;tr class=&quot;r2&quot; valign=&quot;top&quot;&gt;
&lt;td&gt;&lt;strong&gt;Strategies&lt;/strong&gt;&lt;/td&gt;
&lt;td&gt;&lt;strong&gt;:&lt;/strong&gt;&lt;/td&gt;
&lt;td&gt;&lt;ol style=&quot;list-style-type: lower-alpha;&quot;&gt;
&lt;li style=&quot;text-align: justify;&quot;&gt;The water supply tariff in urban areas shall be set to recover operating costs and depreciation and gradually increased to recover the full supply cost of providing service including debt service and a reasonable rate of return.&lt;/li&gt;
&lt;li style=&quot;text-align: justify;&quot;&gt;The water supply tariff in rural areas shall be set to reflect, at a minimum, the cost of sustainable operation and maintenance of the system taking into account any voluntary contributions by the users and, where feasible, include a cost sharing arrangement for the capital investment for system installation and expansion.&lt;/li&gt;
&lt;li style=&quot;text-align: justify;&quot;&gt;The cross subsidy from commercial/industrial consumers to domestic consumers shall be reduced to a reasonable level.&lt;/li&gt;
&lt;li style=&quot;text-align: justify;&quot;&gt;A sewerage tariff that covers operation and maintenance costs shall be introduced in areas served by a sewerage collection system and charged to the user based on water consumption.&lt;/li&gt;
&lt;li style=&quot;text-align: justify;&quot;&gt;The water tariff structure shall make appropriate provisions for low-income urban and rural consumers including an appropriate lifeline tariff to ensure the affordability of a level of water sufficient for basic consumption and hygiene.&lt;/li&gt;
&lt;li style=&quot;text-align: justify;&quot;&gt;Service providers shall be required to reduce costs through increased operational efficiency and reductions in non-revenue water.&lt;/li&gt;
&lt;/ol&gt;&lt;/td&gt;
&lt;/tr&gt;
&lt;tr class=&quot;mhead&quot;&gt;
&lt;td colspan=&quot;3&quot;&gt;Investment&lt;/td&gt;
&lt;/tr&gt;
&lt;tr class=&quot;r1&quot; valign=&quot;top&quot;&gt;
&lt;td&gt;&lt;strong&gt;Objective&lt;/strong&gt;&lt;/td&gt;
&lt;td&gt;&lt;strong&gt;:&lt;/strong&gt;&lt;/td&gt;
&lt;td style=&quot;text-align: justify;&quot;&gt;Develop an investment program based on a mix of private and public funds that prioritizes and allocates investment resources based on socioeconomic criteria and ensure the equitable distribution of investment resources across the country.&lt;/td&gt;
&lt;/tr&gt;
&lt;tr class=&quot;r2&quot; valign=&quot;top&quot;&gt;
&lt;td&gt;&lt;strong&gt;Strategies&lt;/strong&gt;&lt;/td&gt;
&lt;td&gt;&lt;strong&gt;:&lt;/strong&gt;&lt;/td&gt;
&lt;td&gt;&lt;ol style=&quot;list-style-type: lower-alpha;&quot;&gt;
&lt;li style=&quot;text-align: justify;&quot;&gt;Investment in the sector shall be based on the factors such as population density, demand for water, per capita investment, health impacts, coverage levels, socioeconomic factors, and the selection of appropriate technologies.&lt;/li&gt;
&lt;li style=&quot;text-align: justify;&quot;&gt;Investment plans shall be developed by all service providers on a rolling basis taking into account the targets for increasing coverage and improving service quality.&lt;/li&gt;
&lt;li style=&quot;text-align: justify;&quot;&gt;Infrastructure development shall be undertaken in secondary towns and rural areas to slow the rate of migration to major urban areas.&lt;/li&gt;
&lt;li style=&quot;text-align: justify;&quot;&gt;A demand driven approach shall be utilized to ensure the investments are desired by the community and are affordable.&lt;/li&gt;
&lt;li style=&quot;text-align: justify;&quot;&gt;The grant/loan ratio in urban areas shall be rationalized and gradually reduced.&lt;/li&gt;
&lt;/ol&gt;&lt;/td&gt;
&lt;/tr&gt;
&lt;tr class=&quot;mhead&quot;&gt;
&lt;td colspan=&quot;3&quot;&gt;Source Protection and Water Conservation&lt;/td&gt;
&lt;/tr&gt;
&lt;tr class=&quot;r1&quot; valign=&quot;top&quot;&gt;
&lt;td&gt;&lt;strong&gt;Objective&lt;/strong&gt;&lt;/td&gt;
&lt;td&gt;&lt;strong&gt;:&lt;/strong&gt;&lt;/td&gt;
&lt;td style=&quot;text-align: justify;&quot;&gt;Adopt a holistic approach for source protection and water conservation to ensure a concerted effort by to protect drinking f water resources, ensure adequate supply, the encourage the conservation, reclamation and reuse of water, and minimize the impact of wastewater discharges.&lt;/td&gt;
&lt;/tr&gt;
&lt;tr class=&quot;r2&quot; valign=&quot;top&quot;&gt;
&lt;td&gt;&lt;strong&gt;Strategies&lt;/strong&gt;&lt;/td&gt;
&lt;td&gt;&lt;strong&gt;:&lt;/strong&gt;&lt;/td&gt;
&lt;td&gt;&lt;ol style=&quot;list-style-type: lower-alpha;&quot;&gt;
&lt;li style=&quot;text-align: justify;&quot;&gt;Water demand management programs shall be implemented by service providers including programs that educate consumers about water conservation and water saving techniques.&lt;/li&gt;
&lt;li style=&quot;text-align: justify;&quot;&gt;In times of water scarcity, drinking water will have priority over other uses.&lt;/li&gt;
&lt;li style=&quot;text-align: justify;&quot;&gt;The reuse and re-processing of water and use of alternative water sources for non-consumptive purposes shall be encouraged&lt;/li&gt;
&lt;li style=&quot;text-align: justify;&quot;&gt;Government agencies, service providers and any person shall cooperate and participate in programs to protect drinking water sources and reduce the impact of wastewater discharges to inland and coastal waters.&lt;/li&gt;
&lt;/ol&gt;&lt;/td&gt;
&lt;/tr&gt;
&lt;tr class=&quot;mhead&quot;&gt;
&lt;td colspan=&quot;3&quot;&gt;Quality Assurance and Capacity Building&lt;/td&gt;
&lt;/tr&gt;
&lt;tr class=&quot;r1&quot; valign=&quot;top&quot;&gt;
&lt;td&gt;&lt;strong&gt;Objective&lt;/strong&gt;&lt;/td&gt;
&lt;td&gt;&lt;strong&gt;:&lt;/strong&gt;&lt;/td&gt;
&lt;td style=&quot;text-align: justify;&quot;&gt;Enhance the service of quality through the implementation of the following programs in order to improve the capacity of service providers including community groups in building &amp;amp; operation of water supply &amp;amp; sanitation facilities.&lt;/td&gt;
&lt;/tr&gt;
&lt;tr class=&quot;r2&quot; valign=&quot;top&quot;&gt;
&lt;td&gt;&lt;strong&gt;Strategies&lt;/strong&gt;&lt;/td&gt;
&lt;td&gt;&lt;strong&gt;:&lt;/strong&gt;&lt;/td&gt;
&lt;td&gt;&lt;ol style=&quot;list-style-type: lower-alpha;&quot;&gt;
&lt;li style=&quot;text-align: justify;&quot;&gt;Water quality of service provision shall be monitored by the Water Sector Regulatory Commission or its designate.&lt;/li&gt;
&lt;li style=&quot;text-align: justify;&quot;&gt;It shall be the responsibility of service providers to ensure compliance with national drinking water standards.&lt;/li&gt;
&lt;li style=&quot;text-align: justify;&quot;&gt;A certification program shall be developed and implemented for water quality testing laboratories of service providers and others.&lt;/li&gt;
&lt;li style=&quot;text-align: justify;&quot;&gt;A quality assurance program for construction, materials, and quality shall be developed and implemented for service providers.&lt;/li&gt;
&lt;/ol&gt;&lt;/td&gt;
