&lt;h5&gt;Investment Scenarios&lt;/h5&gt;
&lt;p style=&quot;text-align: justify;&quot;&gt;Once the needs are prioritized in the urban sector in consideration of coverage and service levels, investment requirements shall be decided for the planned horizon. Financing could be categorized in to following areas.&lt;/p&gt;
