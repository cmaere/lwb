		<summary type="html">&lt;p&gt;A LEADING JAPANEESE CONSTRCTION COMPANY CONSTRUCTS A 2000m3 CAPACITY AND 10M HIGH PRE - STRESSED CONCRETE (PC) &amp;nbsp; WATER TANK AT BERUWALA FOR NATIONAL WATER SUPPLY &amp;amp;DRAINAGE BOARD, UNDER &amp;nbsp;JICA ASSISTANCE.&lt;/p&gt;
&lt;p&gt;&lt;span style=&quot;font-size: 12.1599998474121px; line-height: 1.3em;&quot;&gt;A leading Japanese construction company; ABE Nikko Kogyo Co., which was &amp;nbsp;established in 1949 and &amp;nbsp;specialized in pre stressed concrete structures entered into an agreement &amp;nbsp;with a Sri Lankan construction company, KDA Weerasinghe in Colombo on 14th August 2015 to construct 2000m3 capacity and 10m high pre - stressed concrete ground reservoir at &amp;nbsp;Beruwala.&lt;/span&gt;&lt;/p&gt;
&lt;table&gt;
&lt;tbody&gt;
&lt;tr&gt;
&lt;td&gt;&amp;nbsp;&lt;img src=&quot;http://waterboard.lk/web/images/contents/news/WatertankBeruwala/image001.png&quot; alt=&quot;image001&quot; width=&quot;343&quot; height=&quot;277&quot; /&gt;&lt;/td&gt;
&lt;/tr&gt;
&lt;/tbody&gt;
&lt;/table&gt;
&lt;p&gt;This ground reservoir will serve to enhance the water supply facilities around Beruwala. This is a pilot project and required finances as well as the technical know-how for the project are provided by ABE Nikko Kogyo Co. through JICA assistance for improving the water supply facilities in Sri Lanka.&lt;br /&gt;In Sri Lanka, very few PC water retaining structures have been constructed. However PC structures have the following advantages over conventional cast in-situ reinforced concrete structures in water supply systems. &lt;br /&gt;1. Economical for capacities of more than 3000m3 when compared to Reinforced concrete structures.&lt;br /&gt;2. Can be designed for no cracks. Hence no leakages.&lt;br /&gt;3. No limitations for depth of the structures.&lt;br /&gt;4. Required less land area&lt;br /&gt;5. Shorter construction period&lt;br /&gt;6. High durability and less maintenance &lt;br /&gt;7. It has proven resistance to earth quakes and Tsunami situations.&lt;/p&gt;
&lt;p&gt;Apart from above ABE Nikko Kogyo Co. uses new technology called Air Dome Engineering for constructing the dome roof of the structure where heavy form work and false work system are not required; thus saving time.&lt;/p&gt;
&lt;p&gt;Moreover, the technical know-how in design and construction of PC water tanks will be transferred to National Water Supply &amp;amp; Drainage Board as well as to the local contractors. This new technology will support to cater for the ever increasing demand of pipe borne water in the country as it has definite advantages over conventional cast in-situ concrete .&lt;/p&gt;
&lt;p&gt;The local contractor MS KDA Weerasinghe has mobilized to the site at Beruwala and construction is ongoing. The construction works will be expected to complete in February 2016.The technical staff of the NWS&amp;amp;DB as well as the interested local contractors can visit the site by contacting DGM (western South ) ,NWS&amp;amp;DB through following contact numbers and by prior appointment;&amp;nbsp;011-2224888/0777725294&lt;/p&gt;
