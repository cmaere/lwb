<p style="text-align: justify;">Usually the effective volume of a septic tank shall be three times the daily inflow and as such a family of 6 shall have a tank of nearly 3 m3 capacity - A septic tank shall be completely water tight and shall have ventilation pipes extended beyond the roof level of the building. The tank shall be a structurally sound to withstand the internal and external forces and shall have access for maintenance and cleaning purposes. Municipal laws prohibit the discharge of wastewater in to the open drains.</p>
<h4>Soakage Pits</h4>
<p style="text-align: justify;">Soakage pits are used to soak septic tank effluent in to the surrounding soil. They do not provide any direct treatment and are based on the principle that the effluent gets treated as it passes through the surrounding soil before entering the ground water table or other water body. Therefore following shall be noted in building and using soakage pits. Soakage pits shall be</p>
<ul>
<li style="text-align: justify;">at least 18 m away from a well or other drinking water source</li>
<li style="text-align: justify;">at least 5 m from the nearest building</li>
<li style="text-align: justify;">at least 10 - 20 m from any other soakage pit</li>
<li style="text-align: justify;">at least 1.5 m shall be kept between the bottom of the tank to the seasonal ground water table</li>
<li style="text-align: justify;">Adequate contact area with the surrounding soil to absorb the effluent in to the soil. In clayey soil, larger pits will be needed.</li>
<li style="text-align: justify;">Adequate openings shall be left in the walls of the pit to have the contact with the surrounding soil</li>
</ul>
<p style="text-align: justify;">If a soakage pit cannot be constructed due to above reasons there are following measures in place of a soakage pit</p>
<ul>
<li>Seepage beds</li>
<li>Seepage trenches</li>
<li>Anaerobic bio filters</li>
</ul>
