			<description><![CDATA[<h4>Septic Tanks - What are they?</h4>
<p style="text-align: justify;">Before we talk about septic tanks, it is worth knowing why we need a septic tank. As we all know, the septic tank receives what we all do not want; that is sewage. Although we don't need sewage, if the matter is left unattended or not given the due priority that it deserves, the mayhem that can cause may keep you worried until you dispose it safely.</p>
<p style="text-align: justify;">Often the disposal of sewage is a real challenge to the house builders and all of us have to face the problem of building of our own house sooner or later. The building of houses keeping provision for safe disposal of sewage is a very important aspect in building. It is human nature that we tend to think more about what happens first than what happens last.</p>
<p style="text-align: justify;">Sewage disposal is often given the least priority and in most of the cases we want to do something for it and forget about it. However if the thing is not done at once in a way it demands that can make you and your environment polluted. Therefore it is extremely useful to think about disposal of sewage in an environmentally friendly manner in either new house building and or maintenance of the existing houses. This will keep you relaxed and saves lot of money in maintaining the services later on.</p>
<h4>Sewage</h4>
<p style="text-align: justify;">Sewage is wastewater of a community. It may be purely domestic origin or it may contain some industrial, commercial or agricultural waste water as well.</p>
<p style="text-align: center;"><em><strong>Wastewater = Domestic sewage + industrial/ commercial/ institutional sewage</strong></em></p>
<h4>Domestic Sewage</h4>
<p style="text-align: justify;">Domestic sewage is the collection of human body waste (faeces + urine [Black water]) and wastewater resulting from personal washing, laundry, food preparation and cleaning of kitchen utensils (Grey water).</p>
<p style="text-align: justify;">Contrary to the domestic sewage, industrial and commercial sewage can contain harmful substances to the human health both in short term and in long term. Heavy metals from factories and radioactive substances from hospitals can be cited as examples.</p>
<h4>Why Treat Sewage?</h4>
<p>Treatment of sewage is essential,</p>
<ol>
<li>To prevent the pollution of;
<ul>
<li>Ground water</li>
<li>Surface water</li>
<li>Marine environment</li>
<li>Flora &amp; fauna</li>
<li>General environment</li>
</ul>
</li>
<li style="text-align: justify;">To reduce the spread of communicable diseases caused by the pathogenic organisms in the sewage such as Cholera, typhoid, diarrhea, intestinal worms etc.</li>
</ol>
<p>The recent out break of Dengue, Malaria, and Japanese Encephalitis etc are examples.</p>
<h4>Method of Treatment</h4>
<p style="text-align: justify;">The method of treatment depends on quality of sewage to be treated. Domestic sewage is the simplest form of sewage and that contribute to the largest volume in a municipal sewerage scheme. Sri Lankan households are not connected to public sewer systems in most of the areas due to unavailability of such service and therefore we are compelled to treat the sewage at the point of generation.</p>
