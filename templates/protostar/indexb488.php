<p>What is the term of NRW? <br />Non-Revenue Water (NRW) = “Net Production” - “Revenue Water”<br />The difference between the volumes of water delivered in to a Network and billed authorized consumption</p>
<p><br />How can create a NRW? <br />Real Losses <br />- Distribution main Leaks (Visible/Invisible/Un reported)<br />- Transmission main Losses (Visible/Invisible/Un reported)<br />- Reservoirs Leakage &amp; Over Flows.</p>
