			<description><![CDATA[<h4>Introduction</h4>
<p style="text-align: justify;">It was a significant milestone for National Water Supply &amp; Drainage Board to start the Mapping Section in the year 1980 with the objective of preparing and updating of maps especially for the water distribution network which carried them out manually by the technical staff. The GIS &amp; Mapping Section was established in the present form in the year 2000 with the main objective of serving as the main agency in surveying, mapping and in geo-spatial activities having a particular responsibility in the organization. .(These implementations were carried out under“Third Water Supply and Sanitation Project – Package C – Mapping Component : NWS&amp;DB – funded by NORAD)</p>
<p style="text-align: justify;">The GIS &amp; Mapping Section plays a leading role in most of the geo-spatial and mapping activities within the NWS&amp;DB. A few major contributions are surveys and mapping of water distribution network, digitization of as-built drawings and analogue drawings using latest techniques, upgrading same to a higher level of accuracy with the introduction of high accuracy Global Positioning System (GPS) and surveying consumer locations. It also contributed to the “Third Water Supply and Sanitation Project – Package C – Mapping Component (NWS&amp;DB). It is significant that their contribution in capacity development activities and supply of technical expertise for geo-spatial activities, publishing the Spatial Data Model for NWS&amp;DB, and also takes initiatives towards building up of Spatial Data Infrastructure( SDI ) and GIS for NWS&amp;DB.</p>
<p style="text-align: justify;">Further, the GIS &amp; Mapping Section is enriched,equipped with the latest technology in the field of geo-spatial information handling and management. At all the times, it does research and development work in exploring geo-spatial science and technology to cope up with the rapid changes in new technology that contributes for the improvement of the organization and country’s development towards the well being of the nation.</p>
<p style="text-align: justify;">The GIS &amp; Mapping Section consists of the Engineering Services and Mapping Services branches.They are responsible for the development of Geographic Information System for NWS&amp;DB. Other main duties include creation of ArcGIS maps for NWS&amp;DB and for other organizations when requested. The GIS &amp; Mapping Section is also responsible in storing and maintaining GIS maps of the water distribution network as well as the Sewer Network of NWS&amp;DB.</p>
<p style="text-align: justify;">The process sets the scene for promoting a culture of integration and collaboration within NWS&amp;DB and its leadership and commitment has the potential to ‘pave the way’ for new approaches to the way geo-spatial activities are conducted in the NWS&amp;DB arena in the future.</p>
<h4 style="text-align: justify;">Underpinning Principles</h4>
<p>• Make geo-spatial data accessible and available to all users within NWS&amp;DB.<br />• Deliver high quality authoritative geo-spatial data to the users of NWS&amp;DB.<br />• Simplified access to the geo-spatial data by providing on-line access using organizations common infrastructure.<br />• Provide basis to improve the efficiency of the organization’s process involving geo-spatial information.<br />• Combined various spatial data themes for the purpose of analysis and decision making.<br />• Optimize the investment and develop a partnership across the geo-spatial information community of the organization.</p>
