			<description><![CDATA[<p>Ground Water Section in the National Water Suppy and drainage Board has been functioning since late 1978. At present the section is operating with the regional RSC set up with direct technical support from the Assistant General Manager (Groundwater) office which is under super vision of DGM(Development) and Addl. GM (Policy &amp; Planning). There are nine regional Grounwater offices for the nine provinces of the country.</p>
<p>&nbsp;</p>
<h6>Main activities and services of the Groundwater section are:</h6>
<ul>
<li>Hydrogeological and geophysical investigations for ground water sources, bed rock levels, saline intrusions and various purposes of underground activities.</li>
<li>Construction of water wells (deep and shallow) using various methods for ground water extraction, well development and supply and installation of Hand pumps and Iron removal plants.</li>
<li>Conducting of pumping tests for recommendation of ground water extraction.</li>
<li>studies on ground water problems, hydrogeological mapping, groundwater management and environmental impact assessments.</li>
<li>Implementation of the national policy 3Tier maintenance system for Hand pumps Tube Wells.</li>
<li>Training of NWSDB staff and outside parties/ personals in connection with various aspects of ground water activities&nbsp;</li>
</ul>
