<h6>&nbsp;</h6>
<h6><span style="color: #00ccff;">&nbsp;<span style="color: #000000;">Main function &nbsp;of the Supplies and Material Management Section :</span></span></h6>
<ol style="line-height: 15.8079996109009px;">
<li style="list-style-type: none;"><ol>
<li>Procument and distribution of chemicals which are used for purification of water through the NWSDB treatment plants.</li>
<li><span style="line-height: 15.8079996109009px;">Procument and distribution of water meters and new connection materials for the all stores of NWSDB.</span></li>
<li>Providing materials for the Head Office.</li>
<li>Getting approval and procurement of capital Items.</li>
<li>Assisting for the importing related activities with regard to Foreign Funded Projects.</li>
<li>Following up of the Materials Management activities of the whole NWSDB.</li>
<li>Following up of stock verification of NWSDB's stores.</li>
<li>Arranging disposal and distraction of unserviceable and obsolete items of NWSDB.</li>
</ol></li>
</ol>
<h6>&nbsp;</h6>
<h6>Main objectives of supplies and material management section</h6>
<ul style="list-style-type: circle;">
<li>Internal and external customer satisfaction.</li>
<li>Provide timely delivery of goods to the internal customer.</li>
<li>Timely payments to external customers.</li>
<li>Maintaining of good service level to internal customers without any stock out situation.</li>
<li>To increase inventory turnover rate.</li>
