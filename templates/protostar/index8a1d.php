<p style="text-align: justify;">Regional Support Centre (Western _Production) provides potable drinking water for Colombo and suburb areas. Deputy General Manager (DGM) heads the Western Production with four Managers (Operational and Maintenance). Civil, Mechanical, Electrical&amp; Electronics Engineers report to the DGM through managers.</p>
<p style="text-align: justify;">The Western - Production region provides service to the other Water Treatment Plants in the island within their capacity.<br />All managers are responsible to maintain water quality compliance to SLS 614:1983 standards.</p>
