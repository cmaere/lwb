<p style="text-align: justify;">The District Engineer (DE) Kurunegala office is located within the same premises as the Manager (O&amp;M) office as a result of which the independence and identity of DE’s office Kurunegala is in question. Most of the coordination and consumer relation functions are commonly handled at both offices but in some instances customers tend to bypass the DE and directly access the Manager (O&amp;M). In contrast, the DE office Puttalam which is located in Chilaw which is far away from the Manager (O&amp;M) Kurunegala office appears to be functioning better. A major portion of the service connections are now in Puttalam district compared to Kurunegala district which has developed only a limited number of water supply systems in the past.</p>
