			<description><![CDATA[<p style="text-align: justify;">In August 2009 the North RSC functions were completely separated from the other RSCs and a DGM was appointed. An AGM was also put in place to provide support in running the RSC. O&amp;M activities come under Jaffna Region and are managed by a regional manager. The main RSC functions are to provide necessary support to the regional office in the day to day O&amp;M activities and to provide help and guidance in the existing scheme rehabilitation and augmentation work.</p>
