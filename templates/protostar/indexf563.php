			<description><![CDATA[<p style="text-align: justify;">A Deputy General Manager (DGM) heads the Eastern RSC with the support of an Assistant General Manager (AGM). Operations and Maintenance (O&amp;M) activities are divided into four regions namely Trincomalee, Ampara, Batticaloa and Akkaraipattu Region and are managed by four different regional managers. Mainly the RSC functions are to provide necessary support to the four regional offices in the day to day O&amp;M activities and to provide help and guidance in the existing scheme rehabilitation and augmentation work.</p>
<p style="text-align: justify;">Trincomalee O&amp;M Manager directly report to DGM and all other O&amp;M Managers report to the DGM through the AGM and all RSC functions are monitored by the Additional General Manager. The RSC provides services to all regional offices within their capacity, any additional help is obtained from the head office (HO) through the Additional General Manager.</p>
<p style="text-align: justify;">Inspite of having functioned for a long time, the RSC still lacks capacity specially in some technical services, such as planning and design activities, electromechanical works etc. This is primarily due to lack of technical staff and other resources. The main reasons for the shortage of staff are the geographical location and the lack of quarters facilities which has made it very difficult for the RSC to appoint the required skilled personnel as only a few are willing to come and work in the area.</p>


