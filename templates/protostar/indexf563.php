			<description><![CDATA[<p style="text-align: justify;">System of Regional Support Centre (RSC) was established for smooth management of Operation &amp; Maintenance (O&amp;M) Section in 1992. This was a recommendation made by the Institutional Development Programme funded by USAID. At the inception, only four RSCs were established for Western, Southern, Central and North Central Provinces. All Water Supply Schemes (WSS) except Kalutara WSS were under the Western RSC. With the expansion of water supply facilities in Western Province, it was decided to divide Western RSC in to 3 RSCs in 2008. Accordingly, RSC- Western Central (WC) was established to coordinate activities related to the central part of Western Province.</p>
<p style="text-align: justify;">At present, RSC-WC geographically covers all areas in Colombo District except Dehiwala-Mount Lavinia and Moratuwa Municipal Council Areas. It consists of 02 Municipal Councils (Colombo &amp; Sri Jayawardhanapura), 05 Urban Councils, 04 Provincial Council areas falling within 08 Divisional Secretary areas. RSC-WC has the largest number of connections (about 365,000 as at May 2012) when compared to all other RSCs. It generates the highest income for NWS&amp;DB and in year 2011 the income was Rs. 5,152 million which was about 40% of the total annual income of NWS&amp;DB.</p>
<p style="text-align: justify;">Organization structure of the RSC-WC is very much similar to the other RSCs with only few exceptions. RSC-WC is headed by the Deputy General Manager (DGM). Owing to the complexity of works involved in, DGM is assisted by three Assistant General Managers (AGMs) separately for Development, O&amp;M and Water Loss Management. RSC had a total of 1,396 Staff as at May 2012.</p>


