			<description><![CDATA[<p style="text-align: justify;">The Deputy General Manager (DGM) heading the North Central RSC is supported by an Assistant General Manager (AGM) in running the RSC unit. O&amp;M activities are looked after by the Anuradhapura Region O&amp;M manager, who is responsible for all the O&amp;M activities of Anuradhapura and Polonnaruwa districts. The RSC functions are mainly to provide necessary support to regional offices for the day to day O&amp;M activities and to provide help and guidance in existing scheme rehabilitation and augmentation work.</p>
<p style="text-align: justify;">The O&amp;M manager reports to the DGM through the AGM but all RSC functions are monitored by Additional General Manager from H/O. The RSC provides services to the regional offices within their capacity and any additional help as necessary is obtained from H/O through the respective Additional General Manager.</p>


