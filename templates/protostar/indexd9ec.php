			<description><![CDATA[<h4>History of the National Water Supply &amp; Drainage Board</h4>
<table>
<tbody>
<tr>
<td style="text-align: justify;">
<p>The Organization had its beginning as a sub department under the Public Works Department, for Water Supply &amp; Drainage. In 1965, it became a division under the Ministry of Local Government. From 1970, this division functioned as a separate department under the Ministry of Irrigation, Power &amp; Highways and remained so until the present Board was established in January 1975 by an Act of Parliament.</p>
<p><br />The National Water Supply Drainage Board (NWS&amp;DB), which presently functions under the Ministry of Urban Development, Water Supply &amp; Drainage is the principal authority providing safe drinking water and facilitating the provision of sanitation in Sri Lanka. In accordance with the NWS&amp;DB Act, a number of major Urban Water Supply Schemes operated by Local Authorities were taken over by the NWS&amp;DB to provide more coverage and improved service. Consumer metering and billing commenced in 1982. Rural Water Supply &amp; Sanitation programmes including deep well programmes are also being implemented by the NWS&amp;DB.</p>
</td>
<td>
<p style="text-align: center;"><a href="http://waterboard.lk/web/images/contents/history.jpg" data-rokbox=""><img style="margin: 10px;" src="http://waterboard.lk/web/images/contents/history.jpg" alt="history" width="300" height="208" /></a></p>
<p style="text-align: center;"><em>The Hon. Maithripala Senanayake, Ministry of Irrigation Power &amp; Highways at the inaugural meeting of the Board on 02-12-1974</em></p>
</td>
</tr>
<tr>
<td colspan="3">
<p>During the past years, the organization has considerably expanded its scope of activities.</p>
<p><br />The NWS&amp;DB is presently operating 324 Water Supply Schemes which cover 34% of the total population with pipe borne water supply. 10.5% of the population is served with pipe borne water supply by some Local Authorities, NGOâ€™s and Community Based Organizations. 13% of the population is served with hand pump tube wells. We hope to increase the coverage with pipe borne water to 45.7% by 2015 so that the United Nations Millennium Development Goal of 85% safe drinking water coverage can be achieved by that year. The NWS&amp;DB is also in charge of the sewerage system in Colombo and suburbs, Hantane, Koggala, Hikkaduwa, Kataragama and in few housing schemes.</p>
</td>
</tr>
</tbody>
</table>]]></description>
