&lt;li&gt;මානව සම්පත් සැලසුම්කරණය හා සංවර්ධනය&lt;/li&gt;
&lt;li&gt;නව්‍ය තාක්ෂණ ක්‍රම භාවිතා කරමින් සේවා වැඩි දියුණු කිරීම සඳහා පර්යේෂණ සහ සංවර්ධනය&lt;/li&gt;
&lt;li&gt;අයවැයකරණය සහ මූල්‍ය පාලනය&lt;/li&gt;
&lt;li&gt;ඵලදායී ජල භාවිතය සහ ආදායම් නොලබන ජල භාවිතය අඩු කිරීම පිළිබඳ ප්‍රචාරය හා පාරිභෝගික දැනුවත්කිරීමේ වැඩසටහන්&lt;/li&gt;
&lt;li&gt;ආයතනික සැලසුම්කරණය සහ උපාය මාර්ගික කළමනාකරණය&lt;/li&gt;
&lt;li&gt;ජලය සහ අපත ජලය තත්ව අධීක්ෂණ සහ පාලනය සඳහා විද්‍යාගාර සේවා&lt;/li&gt;
&lt;li&gt;ප්‍රජා මූල සංවිධාන (ප්‍ර.මූ.ස.), පළාත් පාලන ආයතන, රාජ්‍ය නියෝජිතායතන සහ පුද්ගලික / රාජ්‍ය අංශයේ ආයතන සඳහා තාක්ෂණික සහාය&lt;/li&gt;
&lt;li&gt;භූගත ජල පරීක්ෂණ, ගැඹුරු ළිං, නළ ළිං / අත් පොම්ප සවි කිරීම&lt;/li&gt;
&lt;li&gt;ජල සැපයුම සහ සෞඛ්‍යාරක්ෂාව පිළිබඳ උපදේශන සේවා&lt;/li&gt;
&lt;li&gt;ව්‍යාපෘති සම්පාදනය සහ කළමනාකරණය&lt;/li&gt;
&lt;li&gt;අංශයේ ක්‍රියාධරයන් සහ පාර්ශ්වකරුවන් සමග සම්බන්ධීකරණය&lt;/li&gt;
&lt;li&gt;තොරතුරු කළමනාකරණය සහ වාර්තා කිරීම&lt;/li&gt;
