&lt;h4 style=&quot;text-align: justify;&quot;&gt;මූලික කර්තව්‍ය&lt;/h4&gt;
&lt;ul&gt;
&lt;li&gt;දේශීය අරමුදල් සහ ආධාර ලබා දෙන්නන්ගේ සහාය ඇතිව ජල සම්පාදන සහ සනීපාරක්ෂක ව්‍යාපෘති පිළිබඳ පරික්ෂා කිරීම, සැලසුම් කිරීම, නිර්මාණය සහ ඉදිකිරීම් අධීක්ෂණය, ශාක්‍යතා අධ්‍යයනය, පිරිවැය ඇස්තමේන්තු සකස් කිරීම සහ එබඳු ව්‍යාපෘති සඳහා පාරිසරික ඇගයීම් තක්සේරු සිදු කිරීම&lt;/li&gt;
&lt;li&gt;පාරිභෝගිකයන්ට සතුටුදායක සේවාවක් සැපයීම පිණිස ජලසම්පාදන සහ සනීපාරක්ෂක යෝජනා ක්‍රම ක්‍රියාත්මක කිරීම සහ නඩත්තුව&lt;/li&gt;
&lt;li&gt;බිල්පත් සකස් කිරීම සහ දැරිය හැකි ගාස්තු ව්‍යුහයන් යටතේ මුදල් එකතු කිරීම&lt;/li&gt;
&lt;/ul&gt;
&lt;h4 style=&quot;text-align: justify;&quot;&gt;ද්විතියික කර්තව්‍ය&lt;/h4&gt;
