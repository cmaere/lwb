&lt;p style=&quot;text-align: justify;&quot;&gt;ජාතික ජල සම්පාදන හා&amp;nbsp;ජලාපවහන මණ්ඩලයේ ආරම්භක අවධියේදී, එනම් 1975 සිට 1990 දක්වා කාලය තුලදී නාගරික ප්‍රදේශ, ගම්මාන සහ කර්මාන්ත පුරවර සඳහා පානීය ජලය සැපයුම කෙරෙහි වැඩි අවධානයක් යොමු කරන ලදී. කෙසේ වෙතත්, ජල භාවිතය ඉහළ යාම සහ ස්ථානීය සෞඛ්‍යාරක්ෂණය සඳහා භූමිය ප්‍රමාණවත් නොවීම නිසා පසුගිය වසර 10 තුළ මලාපවහන පද්ධතිය කෙරෙහිද විශේෂ අවධානය යොමු කරන ලදී. වත්මනෙහි ජා.ජ.ස.ජ. මණ්ඩලය කොලොන්නාව, දෙහිවල - ගල්කිස්ස මහ කොළඹ ප්‍රදේශයේ ජා.නි.ස. අධිකාරියේ නිවාස යොජනා ක්‍රම, හන්තාන සහ කතරගම යන ප්‍රදේශයන්හි සනීපාරක්ෂක ව්‍යාපෘති පවත්වා ගෙන යයි. කොළඹට කි‍ලෝමීටර් 100 ක් දකුණින් පිහිටි වෙරළබඩ නගරයක් වූ හික්කඩුව සඳහා මෑතකදී නල මාර්ග සනීපාරක්ෂක පද්ධති පවත්වා ගෙන යයි. කොලඹට කිලෝමීටර් 100 ක් දකුණින් පිහිටි වෙරළබඩ නගරයක් වූ හික්කඩුව සඳහා මෑතකදී නල මාර්ග සනීපාරක්ෂක යෝජනා ක්‍රමයේ මෙහෙයුම් කටයුතු මෑතකදී ආරම්භ කරනු ලැබීය. කටුනායක, සීතාවක, බියගම සහ කොග්ගල ආයෝජන ප්‍රවර්ධන කලාපයන්හි අපත ජලය රැස් කිරීමේ, පිරියම් කිරීමේ සහ බැහැර කිරීමේ පද්ධති ආයෝජන මන්ඩලය වෙනුවෙන් ජා.ජ.ස.ජ.ම. විසින් නඩත්තු කරනු ලැබේ. මාවතගම, පොල්ගහවෙල, මෝදරවිල සහ මීරිගම ආයෝජන ප්‍රවර්ධන කලාපයන් සඳහා පිරියම් කිරීමේ පද්ධති ජා.ජ.ස.ජ.ම. මගින් මේ වන විට ඉදිකරගෙන යනු ලැබේ.&lt;/p&gt;
&lt;p style=&quot;text-align: justify;&quot;&gt;මහනුවර නගර මධ්‍යයේ පරිසර දූෂණය අවම කිරීම සඳහා නල මාර්ග සනීපාරක්ෂක ව්‍යාපෘතියක් ජපන් රජයේ ආධාර යටතේ ක්‍රියාත්මක කිරීමට නියමිතය. පොම්පාගාර සහ පිරියම් කිරීමේ යන්ත්‍රාගාර සඳහා ඉඩම් ඇතුළුව පාරිසරික බලපෑම් ඇගයීම ඇතුළු ශක්‍යතා අධ්‍යයන මේ වන විට අවසන් කරමින් පවති. කෘෂිකර්ම දෙපාර්තමේන්තුවට අයත් පේරාදෙණියේ පිහිටි ඉඩමක පිරියම් කිරීමේ යන්ත්‍රාගාරය පිහිටුවීමට යෝජිත අතර පිරියම් කිරීමෙන් පසු අපවාහ මහවැලි ගඟට මුදා හැරීමට නියමිතය.&lt;/p&gt;
&lt;p style=&quot;text-align: justify;&quot;&gt;ජාඇල - ඒකල සහ මොරටුව රත්මලාන කර්මාන්ත සහ නේවාසික ප්‍රදේශ සඳහා මෑතකදී ස්විඩන රජයේ ආධාර ඇතිව ව්‍යාපෘතියක් සකස් කරනු ලැබීය. ජාඇල ඒකල ප්‍රදේශයේ පල් දෝරු කාණු පද්ධතියක් සහ පිරියම් කිරීමේ යන්ත්‍රාගාරයක් ඉදි කිරීමට නියමිත අතර වත් පිරියම් යන්ත්‍රාගාරයක් මොරටුව - රත්මලාන සඳහා යෝජිත අතර එය නල මාර්ගයක් ඔස්සේ මුහුදට සම්බන්ධ කරනු ඇත. යෝජිත ව්‍යාපෘතිය යටතේ නගරය සහ විශ්ව විද්‍යාලය ඇතුළුව සැලකිය යුතු ප්‍රදේශයක අපත ජලය ඉවත් කිරීම සිදු කරනු ඇත.&lt;/p&gt;
&lt;p style=&quot;text-align: justify;&quot;&gt;කොළඹ නගරයේ උතුරු සහ දකුණු සනීපාරක්ෂක පද්ධතීන්හි කඩිනමින් සිදු කළ යුතු ප්‍රතිසංස්කරණ සඳහා වන ව්‍යාපෘති ඩෙන්මාර්ක් සහ ඔස්ට්‍රියානු රජයන්ගේ ආධාරයෙන් සිදු කිරිමට නියමිතය. ඒ යටතේ මාදම්පිටියේ නව පොම්පාගාරයක් ඉදිකිරීමට සහ උතුරු ව්‍යාපෘතිය යටතේ කොල්ලුපිටියේ සිට මාදම්පිටිය දක්වා ප්‍රධාන සනීපාරක්ෂක නලවල කි.මී. 20 ක ප්‍රතිසංස්කරණ ඇතුළුව පීටර්සන් පටුමගේ පොම්පාගාරය ප්‍රතිසංස්කරණය කිරීමට යෝජිතය. ඊට අමතරව දකුණු ව්‍යාපෘතිය යටතේ වැල්ලවත්තේ නව පොම්පාගාරයක් සනීපාරක්ෂක කාණු පද්ධතියේ කි.මි. 9 ක් ප්‍රතිසංස්කරණය කිරීමටද නියමිතය.&lt;/p&gt;
&lt;p style=&quot;text-align: justify;&quot;&gt;කුරුණෑගල නගරය සඳහා ද ජල සම්පාදන සහ සනීපාරක්ෂක ව්‍යාපෘතියක් සකස් කොට ඇති අතර ඒ සඳහා ඩෙන්මාර්ක් ජාත්‍යයන්තර සංවර්ධන නියෝජිතායතනයේ (ඩනිඩා) මිශ්‍රණය යෝජනා ක්‍රමය යටතේ අරමුදල් සම්පාදනය කිරීම පිණිස හඳුනාගෙන තිබේ. මෙම ව්‍යාපෘතිය මේ වන විට ටෙන්ඩර් අදියරේ පවතී.&lt;/p&gt;
&lt;p style=&quot;text-align: justify;&quot;&gt;යාපනය අර්ධද්වීපයේ ජලසම්පාදන හා සනීපාරක්ෂක ව්‍යාපෘතියේ ශක්‍යතා අධ්‍යයනය කිරීම පිණිස උපදේශන පැවරුමක් ආසියානු සංවර්ධන බැංකුව (ආ.ස.බැං.) සිදුකර ඇත. මෙම අධ්‍යයනය මෙම වසරේදී නිමා කරන ලදී. සුනාමි ආධාර යටතේ වෙරළබඩ නගරයන්හි සනීපාරක්ෂක අවශ්‍යතා අධ්‍යයනය කිරීම සඳහා එක්සත් ජනපද රජය අරමුදල් සපයා තිබේ.&lt;/p&gt;
&lt;p style=&quot;text-align: justify;&quot;&gt;ශ්‍රී ලංකාවේ පරිපාලන අගනගරය වූ ශ්‍රී ජයවර්ධනපුර කෝට්ටේ සඳහා ජා.ජ.ස.ප.ම. විසින් චීන රජයේ සහාය ඇතිව නල මාර්ග මලාපවහන පද්ධතියක් සම්පාදනය කරමින් පවතී. මූලික සැලසුම් සහ ව්‍යාපෘති සම්පාදනය බොහෝ දුරට නිමා වී ඇති අතර කොන්ත්‍රාත් පිළිබඳ සාකච්ඡා අවසන් වීමෙන් පසු ඉදිකිරීම් කටයුතු 2009 වසරේදී ඇරඹීමට නියමිතය. ත්‍රිකුණාමලය, අම්පාර, ගාල්ල සහ කළුතර පිහිටා ඇති සුනාමි නිවාස යෝජනා ක්‍රමයන්හි සනීපාරක්ෂක පහසුකම් සඳහා ටෙන්ඩර් ලේඛන පිලියෙල කිරීම ඇතුළුව සැලසුම් සහ නිර්මාණ කටයුතු සම්පුර්ණ කර තිබේ. ශ්‍රී ජයවර්ධනපුර විශ්ව විද්‍යාලය, හැව්ලොක් සිටි, රත්මලාන සුනාමි නිවාස යෝජනා ක්‍රමය, තෙලිප්පලෙයි පිළිකා රෝහල සහ රණාල නිවාස යෝජනා ක්‍රමය සඳහා ද මෙබඳු සැලසුම් සකස් කරමින් පවතී.&lt;/p&gt;
&lt;p style=&quot;text-align: justify;&quot;&gt;ජා.ජ.ස.ජ.ම. විසින් නඩත්තු කරනු ලබන සියලු සනීපාරක්ෂක යෝජනා ක්‍රම සඳහා 2008 අප්‍රේල් 01 වන දා සිට ක්‍රියාත්මක වන පරිදි ජා.ජ.ස.ජ. මණ්ඩලය විසින් මලාපවහන සේවා ගාස්තුවක් පනවා තිබේ. වත්මනෙහි ජා.ජ.ස.ජ. මණ්ඩලය කලින් සඳහන් කළ සනීපාරක්ෂක යෝජනා ක්‍රමයන්ට අමතරව නිවාස ඒකක 10,000කින් පමණ යුත් විශාල නිවාස ක්‍රම හයක (මත්තේගොඩ, සොයිසාපුර, ජයවඩනගම, මද්දුමගේවත්ත, රද්දොළුගම සහ හන්තාන) මලාපවහන කාණු පද්ධති නඩත්තු කරයි. තවද, නව සනීපාරක්ෂක ව්‍යාපෘති සඳහා ණය පහසුකම් ලබා දීමට නම් මලාපවහන සේවා ගාස්තුවක් හඳුන්වා දිය යුතු බව විදේශීය ණය ප්‍රදාන ආයතන අවධාරණය කර තිබේ. දැනට පවතින පද්ධතිවල මෙහෙයුම් සහ නඩත්තු පිරිවැය ආවරණය කිරිම පිණිස මෙන්ම අනෙකුත් නාගරික ප්‍රදේශවලට මලාපවහන නල මාර්ග පහසුකම් ක්‍රමිකව ව්‍යාප්ත කිරිම පිණිස මෙබඳු ගාස්තුවක් පැනවීම අත්‍යවශ්‍ය වි තිබේ.&lt;/p&gt;
