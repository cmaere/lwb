&lt;p style=&quot;text-align: justify;&quot;&gt;සමස්ත ව්‍යවසාය ආවරණය කරමින් ක්‍රියාත්මක තොරතුරු තාක්ෂණ ව්‍යාපෘතියට අදාල කටයුතු 2009 වසර තුළදී අඛණ්ඩව පවත්වා ගෙන යනු ලැබීය. සමස්ත ව්‍යවසායෙහි තො.තා. විසඳුම් සඳහා කොන්ත්‍රාත්තුව ඇ.ඩො. මිලියන 4.64 ක මුදලකට ඉන්දියාවේ කො - ඔෆ්ෂන්ස් ටෙක්නොලොජිස් ආයතනය වෙත ප්‍රදානය කරන ලදී.&lt;/p&gt;
&lt;p style=&quot;text-align: justify;&quot;&gt;එයට&amp;nbsp; ජා.ජ.ස.ජ.ම. විසින් මූල්‍යයම පහසුකම් සැපයිමට නියමිත පරිගණක දෘඪාංග උපකරණ සැපයිම සහ සවිකිරිමද අයත් වේ. ජා.ජ.ස.ජ.ම. මෙම ක්‍රියාකාරකම ඉටු කිරීමට බාරගත් අතර පරිගණක දෘඪාංග උපකරණ සැපයීම සහ සවිකිරිම 2007 වසර තුලදී සම්පූර්ණ කරන ලදී.&lt;/p&gt;
&lt;p style=&quot;text-align: justify;&quot;&gt;මෙහි කොන්ත්‍රාත් මුදල පසුව ඇ.ඩො. මිලියන 3.98 දක්වා අඩු කරන ලදී.&lt;/p&gt;
&lt;p style=&quot;text-align: justify;&quot;&gt;2009 වසරේදී විසඳුම සම්පුර්ණ කරනු ලැබීය. විසඳුම ක්‍රියාත්මක කිරීම ඉදිරියේදී සිදු කිරීමට නියමිතය.&lt;/p&gt;
&lt;p style=&quot;text-align: justify;&quot;&gt;31/12/2009 දක්වා එහි සමුච්චිත වියදම ඇ. ඩො. මිලියන 3.98 ක් විය. එයට අමතරව අන්තර්ජාලය ඔස්සේ ගෙවිම් ක්‍රමවේදය, පාරිභෝගික පැමිණිලි ක්‍රමවේදය සහ වත්කම් කළමනාකරණ ක්‍රමවේදය පිළිබඳ විසඳුම් ව්‍යාපෘතිය 2009 වසරේදී සිදු කරමින් පැවතුණි. ස්ථාවර වත්කම් සත්‍යාපනය තක්සේරු දෙපාර්තමේන්තුව විසින් සිදු කරන ලදී.&lt;/p&gt;
