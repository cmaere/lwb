<div class="accordion-inner panel-body">
<p style="text-align: justify;">There are free water common outlets in the distribution system to serve the population who do not have legal water supply. These outlets have been installed when the water was supplied free by the local authority. The current trend to have a green environment by reduction of wastage is managed by measuring the quantity of water consumed and sending monthly bill. To encourage persons to obtain individual connections a program called “Randya” was initiated in 2000.</p>


