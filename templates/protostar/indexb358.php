			<title>Non Revenue Water</title>
			<link>http://waterboard.lk/web/index.php?option=com_content&amp;view=article&amp;id=87&amp;catid=34&amp;Itemid=101&amp;lang=ta</link>
			<guid isPermaLink="true">http://waterboard.lk/web/index.php?option=com_content&amp;view=article&amp;id=87&amp;catid=34&amp;Itemid=101&amp;lang=ta</guid>
			<description><![CDATA[<p style="text-align: justify;">In any water supply system, water that produced and released to the distribution system does not fully generate revenue for the Water Authority. This is mainly due to the loss of water through the distribution system. The quantity of water that does not produces any revenue is usually termed as Non Revenue Water (NRW). This is usually expressed as a percentage of water supplied to the system. While NRW is a threat to the financial sustainability of Water Authority, it is commonly used as a key performance indicator in a Water Authority.</p>
<p style="text-align: justify;">The average NRW figure for Western Central RSc is about 36% during the year 2014. While Towns East of Colombo (North an South) have comparative low NRW figures around 18%, Colombo City records a substantially high value for NRW around 47%. This is mainly due to the large number of water leaks occurring daily in the distribution system due to the aging of pipes. The other contributory factors for NRW are free water supplied to the people living in tenement gardens, theft of water due to illegal connections and bypasses and losses due to administrative problems including defective meters, not taking meter readings, unmetered premises etc.</p>
<p>Following major steps have been taken during the last few years to reduce NRW,</p>
<ul>
<li>Strengthening of attending pipe leaks.</li>
<li>Replacement of corroded pipes.</li>
<li>Provision of individual connections in tenement gardens and reducing public stand posts.</li>
<li>Strengthening of raiding and prosecution of illegal water users.</li>
<li>Replacement of defective water meters.</li>
<li>Improving in metering and billing process to reduce NRW.</li>
</ul>]]></description>
