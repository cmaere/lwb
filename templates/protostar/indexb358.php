<a class="accordion-toggle" data-toggle="collapse" data-parent="#set-nn_sliders-1" href="http://waterboard.lk/web/index.php?option=com_content&view=category&id=34&Itemid=101&lang=ta&format=feed&type=rss#randya-program"><div class="clo"></div><div class="ope"></div><span class="nn_sliders-toggle-inner">
Randya Program
</span></a>
</div>
<div class="accordion-body collapse" id="randya-program">
<div class="accordion-inner panel-body">
<p style="text-align: justify;">Randya program was initiated in year 2000 by providing individual connections to the underserved settlement with individual water connection after disconnecting of common out lets. The residence had to pay affordable charge of Rs 4000/+ plus VAT for the connections. This was further eased by payment being made in installments consisting of down payment of Rs 250/= + VAT ( Rs 480 /= ) as the first installment and balance in 37 equal installments of Rs 100/= . The community was expected contribute by excavation of trenches for pipe laying.</p>
