Unauthorized Consumption of Water Reduction
</span></a>
</div>
<div class="accordion-body collapse" id="unauthorized-consumption-of-water-reduction">
<div class="accordion-inner panel-body">
<p style="text-align: justify;">All citizens of the country have the right to request for an individual drinking water supply. National Water Supply &amp; Drainage Board (NWSDB) evaluates the request based on the availability of distribution system, availability of water system pressure etc. To install the water connection and send the bill, address proof is needed. It could be in the form of assessment tax or number assigned by the local authority.</p>
<p style="text-align: justify;"> A legal water supply would have a connection provided by NWSDB, quantity of water consumed will be measured by a meter, having NWSDB marked on it. Property of the NWSDB ends with the meter. Only authorized persons could tamper with NWSDB property, in the event unauthorized person tampers with NWSDB property it is a punishable offence. It is the responsibility of the water user to ensure that all the consumption are quantified by the meter.</p>
<p style="text-align: justify;">Employees of the NWSDB have the authority to enter private premises to check legality of consumption of water. NWSDB has a separate section specialist in detection of unauthorized consumption. On average 15,000 premises are checked and over 1,500 detection are made.</p>
